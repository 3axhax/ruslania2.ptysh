            <h2 class="title_about">
                Ruslania Books SL. : para todos los interesados en Rusia y la cultura rusa
            </h2>

            Ruslania Books fue fundada en 1986 en Helsinki, Finlandia. La compania fue establecida con el proposito de publicar el “Diccionario de agricultura fines-ruso”. Desde entonces, Ruslania ha publicado otros 3 diccionarios y el libro de Mika Waltari “Sinuhe” en ruso.
            <br><br>
            
            Hoy en dia, Ruslania es uno de los mayores vendedores y distribuidores de libros, peliculas, musica y prensa diaria rusa. Ademas, nuestra compania contiene probablemente la seleccion mas grande de partituras rusas fuera de Rusia. 
            <br><br>
            
            Ya que el mercado finlandes es bastante limitado, en los ultimos anos Ruslania ha hecho incapie en desarrollar las oportunidades de negocio internacional. Los clientes de Ruslania provienen de todos los continentes. Gracias tambien a la ubicacion estrategica de Finlandia, la compania tiene excelentes conexiones no solo con Rusia, sino que es tambien capaz de realizar entregas rapidas y fiables a cualquier pais del mundo.
            <br><br>
            
            Ruslania se especializa en la distribucion de productos rusos. Nuestra seleccion tambien incluye articulos de comercio en otros idiomas, pero esos productos estan siempre relacionados de alguna manera con Rusia o el idioma ruso. Por ejemplo, nuestro surtido contiene libros traducidos del ruso a otras lenguas, numerosos diccionarios, catalogos de negocios rusos, etc.
            <br><br>
            
            La estructura de nuestra compania es muy simple y no contiene costos anadidos burocraticos. Esto se refleja primeramente en nuestros precios. Ademas, Ruslania puede ofrecer un servicio mucho mas personal que la mayoria de nuestros competidores. Nosotros siempre estamos al alcance de nuestros clientes.
