            <h3 class="title_about"> Ruslania Books se especializa en los siguientes productos:</h3>

            <b>Libros.</b> Ruslania se fundo como una libreria. Consecuentemente, los libros son el pilar de nuestro negocio. La compania contiene una gran seleccion de diccionarios, libros de texto, libros de arte, etc. Sobre todo, es siempre muy actual la seleccion de nuestros libros de ficcion y no ficcion.
            <BR><BR>
            
            <B>Prensa diaria.</B> Nuestra division de prensa diaria ofrece mas de 5 000 titulos de periodicos y revistas rusas. Ademas, nuestra seleccion incluye prensa en otros idiomas eslavos. 
            <BR><BR>
            
            <B>Partituras.</B> La coleccion de partituras de Ruslania es, sin ninguna duda, la mas interesante del mundo fuera de Rusia. Nuestra division de partituras tiene buenos contactos con las editoriales mas grandes y activas del mercado ruso. Nuestra cooperacion con los grandes vendedores rusos es otra ventaja que posee la compania. 
            <BR><BR>
            
            <B>Videos.</B> Una cuestion importante cuando se trata de un film en DVD - es el codigo de la region. Ruslania trata de garantizar una amplia coleccion de peliculas. Con todo eso, no todos los DVD producidos en Rusia son “multizona”. Por eso durante la seleccion de un DVD hay que tener en cuenta que, aunque muchos de nuestros DVD son “multizonales”, hay otros que son de la zona 5 (Rusia) o zona 2 (Finlandia y Europa Occidental). Sin embargo, todos los DVD sin excepcion se pueden reproducir en un ordenador.  
            <BR><BR>
            
            <B>Musica.</B> Ruslania mantiene una gran seleccion de musica clasica rusa. Ademas, gracias a nuestros especialistas, la compania ofrece lo ultimo de la musica pop rusa y ucraniana.
            <BR><BR>
            
            <B>Mapas.</B> Ruslania ofrece mapas electronicos para los equipos Garmin. Ruslania es el importador lider de estos productos de Rusia a Finlandia y a toda la Union Europea. Nuestra seleccion tambien incluye mapas impresos y mapas de paredes.
            <BR><BR>
            
            <B>Carteles, tarjetas postales, sellos, calendarios, etc.</B> Ruslania tiene mucha experiencia de importacion y traduccion de carteles y tarjetas de propaganda sovietica. Estos productos son muy populares entre nuestros clientes. Al mismo tiempo, Ruslania posee una gran seleccion de sellos autenticos de la era sovietica. 
            <BR><BR>
            
            <B>Software.</B> Ruslania ofrece software de traduccion,  diccionarios, etc.


