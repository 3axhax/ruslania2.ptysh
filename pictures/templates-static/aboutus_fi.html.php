            <h2 class="title_about">
                Ruslania - 30 vuotta venäläistä kirjallisuutta Helsingissä
            </h2>

            <h3>Keitä me olemme</h3>
    Vuonna 1986 perustettu helsinkiläinen kirjakauppa Ruslania Books Oy on 100-prosenttisesti suomalainen perheyritys, joka tuo maahan venäläisiä kirjoja, elokuvia, julisteita, nuotteja, musiikkia ja lehtiä. Ruslania toimii tärkeässä roolissa tuoden maamme venäläisvähemmistön käyttöön entisen kotimaan kielellä kulttuurillisia tuotteita. Lisäksi tuomme venäläisen kulttuurin mittaamattomat aarteet suomalaisten ulottuville painoksina ja tallenteina, joiden laatuun voit luottaa.

    <h3>Tuotteet</h3>
    Tarjoamme venäläisen kulttuurin helmiä, joista kuka tahansa voi nauttia, esimerkiksi tekstitetyt elokuvat ja piirretyt, musiikki, nuotit ja julisteet. Asiantuntemuksemme avulla suuri yleisö saa tietoa venäläisen  nykykulttuurin tarjonnasta. Ruslania toimii eräänlaisena kulttuurisiltana kahden maan välillä.

    <h3>Opettajille</h3>
    Aikaansa seuraavat opettajat saavan Ruslaniasta venäjänopiskelijoilleen mainiota materiaalia. Usein ryhmät opiskelevat suomalaisista oppikirjoista, mutta luokan seinällä saattaa olla kalenteri tai juliste ja tunnin lopuksi saatetaan katsoa venäläistä elokuvaa. Konsultoimme myös opettajia uuden opetusaineiston valinnassa ja toivotamme opiskelijaryhmät tervetulleiksi opintokäynnille!

    <h3>Kustantamo</h3>
    Pitkän taipaleensa aikana Ruslania on kustantanut kahdeksan kirjaa. Maataloussanaston, Sinuhe egyptiläisen venäjäksi, tekniikan sanakirjan, kaksi talouden ja kaupan sanakirjaa, Osmo Jussilan Suomen suuriruhtinaskunta -kirjan venäjännöksen sekä vuonna 2013 venäjännöksen Esa Seppäsen Venäjä vanha tuttu, vaan niin vieras -kirjasta sekä Elina Sopon väitöskirjan perusteella englanninkielisen The Tsars Surgeon -kirjan. Näistä kahden ensimmäisenä mainitun painokset ovat jo lopussa.
    Suomen suuriruhtinaskunta -kirjan aloittama kirjasarja sai ansaitsemansa jatkon syksyllä 2013, kun Ruslania julkaisi Osmo Jussilan Suomen historian suuret myytit -kirjan venäjäksi.

    <h3>Toimitilat</h3>
    Ruslania-kirjakaupan nykyiset toimitilat Bulevardi 7:ssa ovat ainutlaatuiset. Tilat koostuvat myymälästä, toimistosta, varastoista, postitusosastosta, kokoustilasta ja keittiöstä. On kovan suunnittelutyön ansiota, että yritys voi harjoittaa maailmanlaajuista nettikauppaa pääkaupungin keskustasta käsin.

    <h3>Asiakkaat</h3>
    Ruslania palvelee asiakkaitaan myös nettikaupan www.Ruslania.com avulla ja asiakkaat ovat löytäneet yrityksen tuotteet kaikilta mantereilta Tyynenmeren saaria ja Alaskaa myöten.


    <h3>Tervetuloa</h3>
    Toivotamme sydämellisesti tervetulleiksi Ruslaniaan kaikki venäläisestä kulttuurista kiinnostuneet.<br/>
