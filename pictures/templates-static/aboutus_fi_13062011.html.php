    Ruslania Books Oy - ven&auml;l&auml;isen kultturin tavaratalo - jo 25 vuotta!
  </div>

      Kirjakauppa Ruslania Books on ven&auml;l&auml;isten kirjojen, lehtien,
      karttojen, ATK-ohjelmien, videoiden, musiikin ja nuottien maahantuoja ja
      j&auml;lleenmyyj&auml;. Jos olet kiinnostunut ven&auml;l&auml;isest&auml;
      kaunokirjallisuudesta, historiasta, kulttuurista, vapaa-ajan kirjoista tai
      haluat aloittaa tai syvent&auml;&auml; ven&auml;j&auml;n opintoja,
      l&ouml;yd&auml;t meilt&auml; alan kiinnostavimmat uutuudet. Monet
      Ven&auml;j&auml;n-kaupan ammattilaiset k&auml;ytt&auml;v&auml;t asiantuntevaa
      palveluamme sanakirjahankinnoissaan. Sanakirjojen ohella tarjoamme monia
      muitakin tietoteoksia. Ruslaniasta voit tilata useimmat
      Ven&auml;j&auml;ll&auml; ilmestyv&auml;t sanoma- ja aikakauslehdet, my&ouml;s
      erikoisjulkaisut. Edustamme my&ouml;s suurimpia ven&auml;l&auml;isi&auml;
      nuottikustantamoja - meilt&auml; saat edullisesti nuotit alkeista aina
      korkeakoulutasolle saakka. Videoiden ja musiikin kohdalla maahantuomme vain
      ehdottoman luvallisia tuotteita, jotta voimme taata asiakkaillemme
      virheett&ouml;m&auml;n laadun sek&auml; palautusoikeuden. <br>
      <br>
      <br>
      Sijainti Ven&auml;j&auml;n vieress&auml; on kilpailuvalttimme. Asiakkaanamme
      voit unohtaa kuukausia kest&auml;v&auml;t toimitukset. Luotettavien
      tavarantoimittajakontaktien ansiosta pystymme l&ouml;yt&auml;m&auml;&auml;n
      ja toimittamaan asiakkaan tilaamat tuotteet nopeasti ja edullisesti my&ouml;s
      tuoteluetteloidemme ulkopuolelta. Ruslania on osallistunut lukuisien
      sanakirjojen toimitus- ja julkaisuty&ouml;h&ouml;n. Olemme itse kustantaneet
      t&auml;h&auml;n menness&auml; kolme sanakirjaa sek&auml; yhden romaanin
      ven&auml;j&auml;nn&ouml;ksen. Ruslania pysyttelee jatkuvasti ajan hermolla ja
      haluaa tarjota tuoretta tietoa Ven&auml;j&auml;st&auml; kiinnostuneille ja
      ven&auml;l&auml;isten kanssa tekemisiss&auml; oleville, sek&auml; parantaa
      ven&auml;j&auml;nkielisen tieto- ja kaunokirjallisuuden saatavuutta Suomessa
      ja maailmalla. Haluamme olla lempikirjakauppasi!<br>
      <br>
      <br>
                   
