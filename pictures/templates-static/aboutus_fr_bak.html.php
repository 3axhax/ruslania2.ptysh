      <h2>Ruslania Books Corp. - for everyone interested in Russia and Russian culture</h2>


      <h3>About Ruslania</h3>

      <p>
        Ruslania.com - for everyone interested in Russia and Russian culture <br/>
        In Europe and Worldwide!
      </p>


      <h3>Who we are</h3>

      <p>
        Ruslania is one of the largest whole sellers and distributors of Russian books, films, music and
        periodicals. <br/>
        The company maintains probably the largest sales catalogue of Russian sheet music titles in the World.
      </p>

      <h3>Development</h3>

      <p>
        Since the market in Finland is limited, Ruslania has been developing it's international business
        strongly in recent years. <br/>
        The book store has customers in all continents. Thanks to it's strategic location in Finland, the
        company has good connections <br/>
        to Russia as well as very quick and reliable deliveries to all countries.
      </p>

      <h3>Focus</h3>

      <p>
        Ruslania is tightly focused on distributing Russian products. In the selection there are products also
        in other languages, but each title has something
        to do with Russia. Thanks to narrow focus, Ruslania can offer very high standard of service to both
        corporate and private customers interested in Russia.
      </p>

      <h3>Advantages</h3>

      <p>
        The company structure is very simple and there are no high costs for bureaucracy. This can be seen first
        of all in the pricing. Secondly, Ruslania can offer
        much more personal service than most of it's competitors. Ruslania's customer service is always ready
        and willing to take telephone calls and to give you the
        best service.
      </p>

