            <h3>О «Руслании»</h3>

            <p>
                Ruslania.com – для всех, кто интересуется Россией и русской культурой<br/>
                В Европе и по всему миру!
            </p>

            <h3>Кто мы</h3>

            <p>
                «Руслания» один из крупнейших продавцов и дистрибьюторов русских книг, фильмов, музыки и периодических
                изданий.<br/>
                Компания располагает, вероятно, самым крупным в мире каталогом российских нот.
            </p>

            <h3>Развитие</h3>

            <p>
                Поскольку рынок в Финляндии ограничен, «Руслания» интенсивно развивает международный бизнес в последние
                годы.<br/>
                Книжный магазин имеет клиентов на всех континентах. Благодаря ключевым позициям в Финляндии, компания
                имеет хорошие связи с Россией, а также очень быстрые и надежные поставки во все страны.
            </p>

            <h3>Фокусирование</h3>

            <p>Деятельность «Руслании» сосредоточена на распространении Российских товаров. Есть товары и на других
                языках, но каждый из них так или иначе связаны с Россией. Благодаря узкой направленности «Руслании» мы
                можем предложить очень высокие стандарты обслуживания как корпоративных, так и частных клиентов,
                заинтересованных в России.</p>

            <h3>Преимущества</h3>

            <p>
                Структура компании очень проста, без высоких затрат на бюрократию. Это прежде всего отражается на
                ценообразовании. Кроме того, «Руслания» может предложить гораздо больше персонального обслуживания,
                чем большинство ее конкурентов. «Руслания» предлагает своим клиентам высококлассное обслуживание, вы
                можете получить консультацию как лично, так и по телефону, сотрудники магазина всегда с радостью
                вас проконсультируют.
            </p>

            <h3>Нас рекомендуют:</h3>

            <p>«Руслания» предоставляет свои товары для широкого круга клиентов, таких как:</p>
            <ul>
                <li>Университет Хельсинки (Финляндия)</li>
                <li>Гентский университет (Нидерланды)</li>
                <li>Банк Финляндии</li>
                <li>Институт России и Восточной Европы (Хельсинки, Финляндия)</li>
                <li>Финская Центральная криминальная полиция</li>
                <li>Более 2000 финских компаний-экспортеров</li>
                <li>Более 14 000 частных клиентов в Финляндии</li>
                <li>Более 11 000 частных клиентов в других странах</li>
                <li>Более 400 публичных библиотек в Финляндии</li>
                <li>Библиотечное обслуживание организаций в Финляндии</li>
                <li>а также книжные магазины и агентства.</li>
            </ul>

            <h3>Наше местоположение</h3>

            <p>
                «Руслания» распространяет свою продукцию через широкую сеть местных и международных книжных магазинов.
                Проверьте некоторых из них <a href="http://www.ruslania.com/context-131144/entity-1/language-2.html"
