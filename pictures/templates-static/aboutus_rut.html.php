            <h3>O «Ruslanii»</h3>

            <p>
                Ruslania.com – dlja vseh, kto interesuetsja Rossiej i russkoj kul'turoj<br/>
                V Evrope i po vsemu miru!
            </p>

            <h3>Кto my</h3>

            <p>
                «Ruslanija» odin iz krupnejshih prodavtsov i distrib'jutorov russkih knig, fil'mov, muzyki i
                periodicheskih izdanij.<br/>
                Кompanija raspolagaet, verojatno, samym krupnym v mire katalogom rossijskih not.
            </p>

            <h3>Razvitie</h3>

            <p>
                Poskol'ku rynok v Finljandii ogranichen, «Ruslanija» intensivno razvivaet mezhdunarodnyj biznes v
                poslednie gody.<br/>
                Кnizhnyj magazin imeet klientov na vseh kontinentah. Blagodarja kljuchevym pozitsijam v Finljandii,
                kompanija imeet horoshie svjazi s Rossiej, a takzhe ochen' bystrye i nadezhnye postavki vo vse strany.
            </p>

            <h3>Fokusirovanie</h3>

            <p>Dejatel'nost' «Ruslanii» sosredotochena na rasprostranenii Rossijskih tovarov. Est' tovary i na drugih
                jazykah, no kazhdyj iz nih tak ili inache svjazany s Rossiej. Blagodarja uzkoj napravlennosti «Ruslanii»
                my mozhem predlozhit' ochen' vysokie standarty obsluzhivanija kak korporativnyh, tak i chastnyh
                klientov, zainteresovannyh v Rossii.</p>

            <h3>Preimushchestva</h3>

            <p>
                Struktura kompanii ochen' prosta, bez vysokih zatrat na bjurokratiju. Eto prezhde vsego otrazhaetsja na
                tsenoobrazovanii. Кrome togo, «Ruslanija» mozhet predlozhit' gorazdo bol'she personal'nogo
                obsluzhivanija, chem bol'shinstvo ee konkurentov.
                «Ruslanija» predlagaet svoim klientam vysokoklassnoe obsluzhivanie, vy mozhete poluchit' konsul'tatsiju
                kak lichno, tak i po telefonu, sotrudniki magazina vsegda s radost'ju vas prokonsul'tirujut.
            </p>

            <h3>Nas rekomendujut:</h3>

            <p>«Ruslanija» predostavljaet svoi tovary dlja shirokogo kruga klientov, takih kak:</p>
            <ul>
                <li>Universitet Hel'sinki (Finljandija)</li>
                <li>Gentskij universitet (Niderlandy)</li>
                <li>Bank Finljandii</li>
                <li>Institut Rossii i Vostochnoj Evropy (Hel'sinki, Finljandija)</li>
                <li>Finskaja Tsentral'naja kriminal'naja politsija</li>
                <li>Bolee 2000 finskih kompanij-eksporterov</li>
                <li>Bolee 14 000 chastnyh klientov v Finljandii</li>
                <li>Bolee 11 000 chastnyh klientov v drugih stranah</li>
                <li>Bolee 400 publichnyh bibliotek v Finljandii</li>
                <li>Bibliotechnoe obsluzhivanie organizatsij v Finljandii</li>
                <li>a takzhe knizhnye magaziny i agentstva.</li>
            </ul>

            <h3>Nashe mestopolozhenie</h3>

            <p>
                «Ruslanija» rasprostranjaet svoju produktsiju cherez shirokuju set' mestnyh i mezhdunarodnyh knizhnyh
                magazinov. Prover'te nekotoryh iz nih <a
                    href="http://www.ruslania.com/context-131144/entity-1/language-2.html" target="_blank">zdes'</a>.
            </p>

            <p>
                Мagazin «Ruslanija» raspolozhen v samom tsentre goroda Hel'sinki, stolitsy Finljandii. <a
                    href="http://www.ruslania.com/context-584/entity-1/language-2.html" target="_blank">Кontaktnye
                    dannye</a>.
            </p>

            <h3>Amazon</h3>

            <p>S 2007 goda magazin Ruslanija byl aktiven na rynke amazon.com. Кompanija v nastojashchee vremja
                javljaetsja vedushchim postavshchikom tovarov Rossii dlja pol'zovatelej Amazona, za predelami
                sobstvennogo magazina.</p>

            <h3>Istorija</h3>

            <p>Кnizhnaja Кorporatsija Ruslanija byla osnovana v 1986 godu v Hel'sinki, Finljandija. Eto 100% finskij
                semejnyj biznes.</p>

