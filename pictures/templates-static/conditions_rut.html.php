  <b>USLOVIJa POLZOVANIJa VIRTUALNYM MAGAZINOM RUSLANIJa</b>
  </div>

  <br>

  Nizhesledujuschie punkty opisyvajut uslovija, na kotorykh Ruslania Books Corp. (v dalnejshem Ruslanija) 
  osuschestvljaet prodazhu i postavku knig, not, video- i audiokasset, programmnoj, pechatnoj i drugoj 
  produktsii (v dalnejshem «Produktsija»). Otdelno privedeny <a href="http://ruslania.com/conditions_subscription?language=ru">USLOVIJa PODPISKI NA 
  PERIODICHESKIE IZDANIJa</a>. Ruslanija ostavljaet za soboj pravo periodicheski vnosit 
  izmenenija v eti uslovija. Informatsija obo vsekh izmenenijakh publikuetsja na sajte Ruslanii.
  <br><br>
  <b>1. Obschie uslovija vypolnenija zakazov</b>
  <br><br>

  1.1 Klient objazuetsja sobljudat dejstvujuschie uslovija vypolnenija zakazov i postavok produktsii v Finljandiju 
  i drugie strany. Ruslanija prodaet produktsiju organizatsijam i dostigshim sovershennoletija chastnym litsam. 
  Nesovershennoletnie  litsa dlja pokupki produktsii dolzhny imet razreshenie roditelej/popechitelej.
  <br><br>

  <b>2. Registratsija</b>
  <br><br>

  2.1 Produktsiju v virtualnom magazine mozhet kupit tolko zaregistrirovannyj klient. Chastnye litsa 
  soobschajut imja, familiju, adres, nomer telefona i adres elektronnoj pochty, a pri zhelanii – rodnoj jazyk 
  i sferu interesov. Organizatsija dolzhna takzhe soobschit svoj registratsionnyj kod. Kazhdyj polzovatel 
  vybiraet sebe parol. Ruslanija objazuetsja ne peredavat registratsionnye dannye tretim litsam i soderzhat 
  reestr klientov, kak etogo trebuet zakon. Klient mozhet poluchit polnuju informatsiju o tom, kakie ego 
  dannye imejutsja v reestre. Za vedenie reestra otvechaet otdel obsluzhivanija klientov.
  <br><br>

  2.2 Lichnye dannye klienta i ego istorija zakazov zaschischajutsja vybrannym klientom parolem. Klient dolzhen zabotitsja o tom, chtoby vybrannyj im parol ne uznal kto-to drugoj.
  <br><br>

  <b>3. Tseny</b>
  <br><br>

  3.1 Informatsija o vsej Produktsii predstavlena na sajte Ruslanii. Imejuschajasja Produktsija i tseny na nee mogut 
  menjatsja bez predvaritelnogo uvedomlenija. Vse ukazannye na sajte tseny vkljuchajut NDS, prinjatyj v Finljandii 
  (10% na knigi i 24% na ostalnuju produktsiju).
  <br><br>

  3.2 Za dostavku produktsii vzimaetsja plata soglasno vybrannomu sposobu i strane dostavki. Plata rasschityvaetsja 
  srazu zhe pri  oformlenii zakaza. Otdel obsluzhivanija po svoemu usmotreniju mozhet otpravit zakaz odnoj ili 
  neskolkimi partijami, pri etom plata za peresylku vzimaetsja tolko odin raz.
  <br><br>

  3.3 Summa minimalnoj pokupki v virtualnom magazine Ruslanii sostavljaet pjat evro. Ruslanija ostavljaet za 
  soboj pravo menjat summu minimalnoj pokupki.
  <br><br>

  <b>4. Oplata produktsii</b>
  <br><br>

  Varianty oplaty predstavleny v razdele «Oplata» instruktsii <a href="http://www.ruslania.com/context-8264/entity-1/language-2.html">KAK SDELAT ZAKAZ</a> 
  <br><br><br>

  <b>5 Postavka produktsii</b>
  <br><br>

  5.1 Tak kak Ruslanija prodaet, v osnovnom, importiruemuju produktsiju, sroki dostavki obychno ot 2 do 20 dnej, v zavisimosti ot togo, imeetsja li produktsija na sklade i ot vremeni, neobkhodimogo dlja obrabotki zakaza i ego 
  dostavki.
  <br><br>

  5.2 Esli srok dostavki suschestvenno udlinjaetsja, to Ruslanija nezamedlitelno soobschit ob etom klientu. V tom sluchae, 
  esli zakazannuju produktsiju voobsche nevozmozhno poluchit, Ruslanija predlozhit vzamen analogichnuju. Klient mozhet takzhe 
  ispolzovat zaplachennuju summu na pokupku drugoj produktsii po svoemu vyboru.
  <br><br>

  5.3 Esli zakaz sostoit iz neskolkikh edinits produktsii i sroki ikh postavki raznye, paket obychno otpravljaetsja 
  po poluchenii vsego zakazannogo. Po usmotreniju otdela obsluzhivanija Ruslanii  produktsija mozhet byt otpravlena 
  i v neskolkikh partijakh, no plata vzimaetsja tolko odin raz.
  <br><br>

  <b>6. Otvetstvennost</b>
  <br><br>
  6.1 Ruslanija ne otvechaet za prjamoj ili kosvennyj uscherb, nanesennyj klientu kuplennoj produktsiej, neprinjatym 
  zakazom ili zaderzhkoj ego postavki. Ruslanija ne otvechaet takzhe za soderzhanie produktsii, tak kak eta 
  otvetstvennost lezhit na izdatele ili izgotovitele. V ravnoj stepeni oni zhe otvechajut za sobljudenie avtorskikh 
  i k nim priravnivaemykh prav.
  <br><br>

