  <b>USLOVIJA POLZOVANIJA VIRTUALNYM MAGAZINOM RUSLANIJA</b>
  </div>

  <br>

  Nizhesledujushchie punkty opisyvajut uslovija, na kotoryh Ruslania Books Corp. (v daljanejshem Ruslanija) 
  osushchestvljaet prodazhu i postavku knig, not, video- i audiokasset, programmnoj, pechatnoj i drugoj 
  produktsii (v daljanejshem «Produktsija»). Otdeljano privedeny <a href="http://www.ruslania.com/context-4168/entity-1/language-3.html">USLOVIJA PODPISKI NA 
  PERIODICHESKIE IZDANIJA.</a> Ruslanija ostavljaet za soboj pravo periodicheski vnositja 
  izmenenija v eti uslovija. Informatsija obo vseh izmenenijah publikuetsja na sajte Ruslanii.
  <br><br>
  <b>1. Obshchie uslovija vypolnenija zakazov</b>
  <br><br>

  1.1 Klient objazuetsja sobljudatja dejstvujushchie uslovija vypolnenija zakazov i postavok produktsii v Finljandiju 
  i drugie strany. Ruslanija prodaet produktsiju organizatsijam i dostigshim sovershennoletija chastnym litsam. 
  Nesovershennoletnie  litsa dlja pokupki produktsii dolzhny imetja razreshenie roditelej/popechitelej.
  <br><br>

  <b>2. Registratsija</b>
  <br><br>

  2.1 Produktsiju v virtualjanom magazine mozhet kupitja toljako zaregistrirovannyj klient. Chastnye litsa 
  soobshchajut imja, familiju, adres, nomer telefona i adres elektronnoj pochty, a pri zhelanii – rodnoj jazyk 
  i sferu interesov. Organizatsija dolzhna takzhe soobshchitja svoj registratsionnyj kod. Kazhdyj poljazovatelja 
  vybiraet sebe parolja. Ruslanija objazuetsja ne peredavatja registratsionnye dannye tretjaim litsam i soderzhatja 
  reestr klientov, kak etogo trebuet zakon. Klient mozhet poluchitja polnuju informatsiju o tom, kakie ego 
  dannye imejutsja v reestre. Za vedenie reestra otvechaet otdel obsluzhivanija klientov.
  <br><br>

  <b>3. Tseny</b>
  <br><br>

  3.1 Informatsija o vsej Produktsii predstavlena na sajte Ruslanii. Imejushchajasja Produktsii i tseny na nee mogut 
  menjatjasja bez predvariteljanogo uvedomlenija. Vse ukazannye na sajte tseny vkljuchajut NDS, prinjatyj v Finljandii 
  (10% na knigi i 24% na ostaljanuju produktsiju).
  <br><br>

  3.2 Za dostavku produktsii vzimaetsja plata soglasno vybrannomu sposobu i strane dostavki. Plata rasschityvaetsja 
  srazu zhe pri  oformlenii zakaza. Otdel obsluzhivanija po svoemu usmotreniju mozhet otpravitja zakaz odnoj ili 
  neskoljakimi partijami, pri etom plata za peresylku vzimaetsja toljako odin raz.
  <br><br>

  3.3 Summa minimaljanoj pokupki v virtualjanom magazine Ruslanii sostavljaet pjatja evro. Ruslanija ostavljaet za 
  soboj pravo menjatja summu minimaljanoj pokupki.
  <br><br>

  <b>4. Oplata produktsii</b>
  <br><br>

  4.1 Zakazy oplachivajutsja posredstvom bankovskih sistem <a href="http://www.danskebank.fi/en-fi/Personal/Pages/Personal.aspx" class="maintxt" target="new">DanskeBank</a>, <a href="https://www.op.fi/op/private-customers?id=10000&srcpl=1&kielikoodi=en" class="maintxt" target="new">OP-Pohjola</a>, <a href="http://www.nordea.fi/e/val-index.asp" class="maintxt" target="new">Nordea</a> ili kreditnymi kartami posredstvom 
  kompanii <a href="http://www.luottokunta.fi/en/" class="maintxt" target="new">Luottokunta</a> (finskoj natsionaljanoj kompanii, obsluzhivajushchej kreditnye karty). Organizatsii, 
  zaregistrirovannye v stranah ES, mogut priobresti produktsiju s oplatoj po schetu, odnako ot organizatsij, 
  nahodjashchihsja vne ES, Ruslanija ostavljaet za soboj pravo trebovatja predoplatu zakaza.
  <br><br>

  4.2 Pri oplate zakaza posredstvom setevvyh sistem klient pereadresovyvaetsja na zakrytyj sajt vybrannoj 
  sistemy oplaty. Takim obrazom edinstvennye svedenija, kotorye poluchaet Ruslanija posle oplaty klientom zakaza, 
  eto dannye o dne platezha  i ego  odobrenii ili otklonenii.
  <br><br>

  4.3 Prozhivajushchij v Finljandii klient mozhet takzhe oplatitja pokupki s vystavleniem scheta, kotoryj dolzhen bytja 
  oplachen v techenie 14 sutok. Za prosrochku oplaty vzimaetsja penja v razmere 11% godovyh. Za vtorichnoe napominanie 
  o neuplate vzimaetsja dopolniteljano 5 evro. Ruslanija ostavljaet za soboj pravo otkazatja v prodazhe s vystavleniem 
  scheta i ne prinimatja novyh zakazov, poka ne budut oplacheny zadolzhennosti. Ruslanija mozhet takzhe pri 
  neobhodimosti proveritja kreditnuju informatsiju klienta. Vsja produktsija javljaetsja sobstvennostjaju Ruslanii do polnoj 
  oplaty ee stoimosti klientom.
  <br><br>

  <b>5 Postavka produktsii</b>
  <br><br>

  5.1 Tak kak Ruslanija prodaet, v osnovnom, importiruemuju produktsiju, sroki dostavki obychno ot 2 do 20 dnej, v 
  zavisimosti ot togo, imeetsja li produktsija na sklade i ot vremeni, neobhodimogo dlja obrabotki zakaza i ego 
  dostavki.
  <br><br>

  5.2 Esli srok dostavki sushchestvenno udlinjaetsja, to Ruslanija nezamedliteljano soobshchit ob etom klientu. V tom sluchae, 
  esli zakazannuju produktsiju voobshche nevozmozhno poluchitja, Ruslanija predlozhit vzamen analogichnuju. Klient mozhet takzhe 
  ispoljazovatja zaplachennuju summu na pokupku drugoj produktsii po svoemu vyboru.
  <br><br>

  5.3 Esli zakaz sostoit iz neskoljakih edinits produktsii i sroki ih postavki raznye, paket obychno otpravljaetsja 
  po poluchenii vsego zakazannogo. Po usmotreniju otdela obsluzhivanija Ruslanii  produktsija mozhet bytja otpravlena 
  i v neskoljakih partijah, no plata vzimaetsja toljako odin raz.
  <br><br>

  <b>6. Otvetstvennostja</b>
  <br><br>
  6.1 Ruslanija ne otvechaet za prjamoj ili kosvennyj ushcherb, nanesennyj klientu kuplennoj produktsiej, neprinjatym 
  zakazom ili zaderzhkoj ego postavki. Ruslanija ne otvechaet takzhe za soderzhanie produktsii, tak kak eta 
  otvetstvennostja lezhit na izdatele ili izgotovitele. V ravnoj stepeni oni zhe otvechajut za sobljudenie avtorskih 
  i k nim priravnivaemyh prav.
  <br><br>

  <b>7. Vozvrat i obmen produktsii</b>
  <br><br>
  7.1 Obmenjatja ili vernutja kuplennuju v magazine produktsiju mozhno v techenie 14 dnej s momenta ee poluchenija. 
  Vozvrashchaemaja produktsija dolzhna imetja nepovrezhdennyj tovarnyj vid. Pravo vozvrata ili obmena ne 
  rasprostranjaetsja na audio- i videokassety, CD, DVD i programmnye produkty, esli ih originaljanaja upakovka 
  byla narushena zakazchikom. Pravo vozvrata ne rasprostranjaetsja na programmy, poluchennye posredstvom Interneta 
  («skachivaemye» programmy).
  <br><br>

  7.2 Vozvrat produktsii ot organizatsij reguliruetsja sushchestvujushchim zakonodateljastvom. Prezhde, chem vozvratitja 
  produktsiju, organizatsija dolzhna svjazatjasja s otdelom obsluzhivanija (sm. <a href="http://www.ruslania.com/context-584/entity-1/language-3.html">Kontakty</a>).
  <br><br>

  7.3 Esli produktsija vozvrashchaetsja iz-za granitsy Finljandii, klient dolzhen oplachivatja pochtovye rashody 
  (krome   vozvrata brakovannoj produktsii). Pered vozvratom neobhodimo svjazatjasja s otdelom obsluzhivanija.
  <br><br>

  <b>8. Obstojateljastva nepreodolimoj sily (force majeure)</b>
  <br><br>
  Ruslanija ne neset otvetstvennosti za nepostavku izdanij vvidu dejstvija obstojateljastv nepreodolimoj sily, 
  takih kak stihijnye bedstvija, katastrofy, zabastovki, terroristicheskie akty, sabotazh, zapreshchenie eksporta 
  ili importa, dejstvija organov vlasti i drugih analogichnyh obstojateljastv, vvidu kotoryh dejateljanostja Ruslanii 
  ili ee partnerov sushchestvenno zatrudnjaetsja. O nastuplenii takih obstojateljastv klientam soobshchaetsja nezamedliteljano.
  <br><br>

  <b>9. Reshenie sporov</b>
  <br><br>
  Voznikshie v svjazi s etimi uslovijami spory i raznoglasija reshajutsja, v pervuju ocheredja, mezhdu Ruslaniej i klientom. 
  Esli zhe dostizhenie dogovorennosti nevozmozhno, to spory reshajutsja v gorodskom sude g. Heljasinki s primeneniem 
  zakonodateljastva Finljandii.

