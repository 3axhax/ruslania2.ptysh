  OUR POLICY OF SUBSCRIBING TO PERIODICALS
  </div>
  <br>

  The following policy applies to subscriptions placed to periodicals on the site of Ruslania Books Corp. (hereinafter referred to
    as Ruslania) in the Internet:

  <br><br>
  <b>1. General subscription policy</b>
  <br><br>

    <b>1.1</b> Subscriptions are accepted on Ruslania’s site only for periodicals and other editions which appear in the
                  Russian Federation and in the former CIS territory, and in Finland. A client is obliged to follow this
                  policy of subscripion in Finland and abroad, valid at the time of placing a subscription order. Prior to
                  placing his order, the client shall acquaint himself with the policy and accept it. Ruslania retains the right
                  to amend the rules of delivery without prior notice.

     <br><br>

  <b>2. Ordering</b>
  <br><br>

  <b>2.1</b> Subscriptions to a periodical are accepted only from a registered client. The periodical shall be shipped/mailed
          to the address indicated by the Client in his subscription order, whereby he is liable for the accuracy of the
          address. Ruslania shall not be liable for a misdelivery, should a misdelivery result from the inaccuracy of
          address. The consignee may not necessarily be the payer.
  <br><br>

  <b>2.2</b> As a rule, shipment starts from the first day of the month following the subscription order or first possible
        issue. Ruslania sends an acknowledgement of the beginning of the shipment of the periodical. Should the Client
                have special wishes, he may state them in item 4 ”Special wishes” in the ordering process.

  <br><br>

  <b>3. Amendments (changes)</b>
  <br><br>

  <b>3.1</b> In any and all matters related to the subscription (i.e. changes of name subscriber/consignee or address of
        delivery), the Client should contact Ruslania, and not, for example, the publisher or the agent of the periodical.
        Any changes of address should be notified as soon as possible. The change may not become valid from the
        first forthcoming issue, following the notice of the change. Ruslania confirms (sends an acknowledgement) the
        receipt of all changes of address.
  <br><br>

  <b>4. Prices</b>
  <br><br>

  <b>4.1</b> Subscriptions are accepted by Ruslania only for a fixed period of time; the minimum invoicing period is one
        month. Should a periodical appear less than 12 times a year, the subscription order shall be placed on any
        term possible in this case. If a periodical appears e.g. four times a year, the subscription can be placed for
        3, 6, 9 or 12 months. Possible subscriptions terms are shown on our site in the Internet.
  <br><br>

  <b>4.2</b> The prices of a periodical to be shipped outside Finland (as subscribed to via Ruslania) shall depend on the
        shipping address. As a rule, a periodical shipped overseas is more expensive than one shipped to Finland.
        We retain the right to alter the prices in case of alterations made by the publisher or fluctuation of currency
        rates. This however does not apply to subscriptions already placed.
  <br><br>

  <b>5. Payment of subscription</b>
  <br><br>

  <b>5.1</b> A private person shall pay for his subscription via the Internet by using the electronic banking services
        (<a href="http://www.sampo.fi/english/" class="maintxt">Sampo</a>,
        <a href="http://www.nordea.fi/e/val-index.asp" class="maintxt">Nordea</a>in Finland) or a Visa
        or Master card via the <a href="http://www.luottokunta.fi/en/" class="maintxt">Luottokunta</a>, a national
        credit card service company. A corporate client may be invoiced. In case of a non-EU client, Ruslania retains
        the right to request an advance payment (we will not ship the item until your funds clear).

  <br><br>

  <b>5.2</b> Should the invoicing address differ from the shipping address, the Client shall notify us of this at ”Special
        wishes”  (see Ordering, step 4).
  <br><br>

  <b>5.3</b> Should a private person residing in Finland not have access to e-banking services, he may request our
        subscription department to send him an invoice. In the event of overdue payment or other payment default
        and if no agreement has been reached with the client service, Ruslania shall retain the right not to handle new
        orders until the payments have been settled.

  <br><br>

  <b>5.4</b> If the Client is invoiced, the term of payment shall be fourteen (14) days from the date of the invoice, unless
        otherwise agreed between the Client and the Company. Overdue payment is charged at the rate of 11% p.a.
        (as per law) and an additional fee of EUR 5,0 will be added to the total of the invoice in the second overdue
        reminder. Should the Client not respond to the second reminder, the delivery of the periodicals shall be
        suspended and the price of the issued already mailed shall be charged from the Client. If invoiced, an overseas
        client shall also pay the banking expenses.

  <br><br>

  <b>6. Missing issues claim and enquiries</b>
  <br><br>

  <b>6.1</b> All enquiries and reminders concerning subscription shall be addressed to
        periodicals@ruslania.com. Missing issues
        should be claimed as soon as possible, not later than by the time of arrival of the following issue. If possible,
        Ruslania will inform the subscribers about delay of arrival of issues.

  <br><br>
  <b>6.2</b> Should a periodical not appear as result of a technical flaw or similar default, the non-published or
        non-delivered issues shall be compensated by prolonging the subscription of the Client by the corresponding
        number of issues. Ruslania shall not be responsible for damage (direct or indirect) resulting from a technical
        flaw in the shipped issue or a delay in the delivery of any issue.


  <br><br>
  <b>7. Cancellation</b>
  <br><br>
     Due to the fact that the periodicals are of non-Finnish origin, normal cancellation rules do not apply to subscription to
     periodicals through Ruslania. A mid-term cancellation of a subscription is not allowable unless an agreement with the
     publisher has been reached. If the publisher accepts the cancellation and refunds Ruslania with the non-covered term
    of payment of number, the Client may instead subscribe to another edition (periodical) or some other items on sale at
    Ruslania. Any queries concerning cancellation shall be addressed to: periodicals@ruslania.com.

  <br><br>
  <b>8. Waiver </b>
  <br><br>

  Ruslania assumes no liability whatsoever for the content or presentation of any material or resource in the periodical, as
   well as for change of number of pages, apperance or frequency of appearance. Should a periodical terminate its publication
   in the middle of the subscription term, Ruslania shall primarily offer the Client another periodical or any other items on sale in
   the Ruslania bookshop for the amount of money paid for the periodical.

  <br><br>
  <b>9. Force majeure </b>
  <br><br>

  Ruslania shall not assume liability for non-delivery of issues, if caused by force majeure conditions (acts of God, calamities,
   strikes, terrorist acts, sabotage, export or import prohibition, acts of government officials etc., which have significantly
   jeopardized the operations of Ruslanian or its partners). The Client will be immediately notified on the entry of such
   conditions into force.

  <br><br>
  <b>10. Settlement of disputes </b>
  <br><br>

  Disputes and conflicts concerning subscription and its conditions between Ruslania and its Clients shall be settled primarily
    by amicable negotiations. If a settlement cannot be reached, the disputed may be appeled to the Helsinki Disctrict Court
   of Justice. The settlement shall be governed by Finnish law.

