SUSCRIPCIONES CONDICIONES E INSTRUCCIONES  
  </div>
  <br>

Ruslania Books Oy (en adelante Ruslania) y sus clientes acuerdan la aplicación de las siguientes condiciones para la realización de suscripciones a través de Internet. 

  <br><br>
  <b>1. Condiciones generales de suscripción</b>
  <br><br>

    El cliente acepta junto a  cada suscripción las condiciones vigentes 
    de este contrato para cada caso en particular, que rigen tanto para 
    Finlandia como para el extranjero. Antes de realizar una compra, el 
    cliente deberá cerciorarse de haber leído y aceptado las condiciones 
    estipuladas en vigencia.  Ruslania se reserva el derecho de modificar 
    estas condiciones sin previo aviso.

     <br><br>

  <b>2. Suscripción</b>
  <br><br>

  <b>2.1</b>

    Una suscripción sólo puede ser realizada por un cliente registrado en 
    la tienda online.  La revista se enviará a la dirección indicada por 
    el cliente en su orden de suscripción.  El cliente será responsable
     de la validez de la dirección indicada.  Ruslania no se 
    responsabiliza por errores en la entrega de la suscripción como 
    consecuencia de haberse facilitado una dirección errónea.  El envío 
    puede ser dirigido a una dirección distinta a la de aquél que realice 
    el pago. 

  <br><br>

  <b>2.2</b> 

    Normalmente, el envío comenzará el primer día del mes siguiente a la 
    realización de la orden de suscripción  o desde  el primer número que 
    sea posible.  Ruslania le confirmará al cliente el momento desde el 
    cual comenzará la suscripción.  En caso de que el cliente tuviese 
    alguna demanda particular, podrá especificarla al realizar la orden 
    de compra en el punto 4 "notificaciones concernientes al encargo".

  <br><br>

  <b>3. Cambios</b>
  <br><br>

  <b>3.1</b> 

    Ante cualquier cambio relativo a las suscripciones (por ejemplo., 
    cambios en el nombre del suscriptor o en la dirección de envío), 
    el cliente deberá ponerse en contacto con Ruslania, y no, por 
    ejemplo, con el editor o el agente de la suscripción.  Cualquier 
    cambio en la dirección de envío deberá ser notificado tan rápido 
    como sea posible.   Los cambios se harán efectivos a partir del 
    número siguiente a la fecha de notificación.  Ruslania confirmará 
    la recepción de los cambios de dirección.

 <br><br>

  <b>4. Precios</b>
  <br><br>

  <b>4.1</b> Subscriptions are accepted by Ruslania only for a fixed period of time; the minimum invoicing period is one
        month. Should a periodical appear less than 12 times a year, the subscription order shall be placed on any
        term possible in this case. If a periodical appears e.g. four times a year, the subscription can be placed for
        3, 6, 9 or 12 months. Possible subscriptions terms are shown on our site in the Internet.
  <br><br>

  <b>4.2</b> The prices of a periodical to be shipped outside Finland (as subscribed to via Ruslania) shall depend on the
        shipping address. As a rule, a periodical shipped overseas is more expensive than one shipped to Finland.
        We retain the right to alter the prices in case of alterations made by the publisher or fluctuation of currency
        rates. This however does not apply to subscriptions already placed.
  <br><br>

  <b>5. Pago de la suscripción</b>
  <br><br>

  <b>5.1</b> 

    Las Instrucciones detalladas referidas al pago de la suscripción 
    pueden encontrarse en Cómo realizar una orden de compra, debajo de 
    la sección "Forma de pago".

  <br><br>

  <b>6. Consultas y observaciones</b>
  <br><br>

  <b>6.1</b> 

    Toda consulta u observación referente a la suscripción deberá ser 
    dirigida a periodicals@ruslania.com.   En el caso de que un ejemplar 
    no arribase, rogamos informar tan rápido como sea posible, como máximo
     al arribo del número siguiente. Ruslania informará a la brevedad 
    sobre retrasos en el envío de ejemplares u otros tipos de 
    inconvenientes.

  <br><br>
  <b>6.2</b> 

    En caso de que un número no apareciese por causa de un desperfecto 
    técnico o falla similar, los ejemplares no publicados o no enviados 
    serán compensados prolongando la suscripción del cliente con el 
    número correspondiente de ejemplares no recibidos.  Ruslania no 
    responde por los perjuicios directos o indirectos producidos por 
    el eventual estado defectuoso de los productos o por retrasos de 
    entrega.

  <br><br>
  <b>7. Cancelaciones</b>
  <br><br>

    Como las publicaciones no son de origen finlandés, la reglamentación 
    general referente a las cancelaciones no es aplicable a las 
    suscripciones realizadas a través de Ruslania.  Una suscripción 
    en curso solamente podrá cancelarse con acuerdo del editor.  
    Si el editor aceptara la cancelación y la devolución del importe 
    correspondiente al período de suscripción aún no disfrutado, el 
    cliente podrá encargar por la suma en cuestión otra suscripción o 
    cualquier otro producto de la librería.  Por cualquier consulta 
    referida a la cancelación de suscripciones dirigirse a: 
    periodicals@ruslania.com.

  <br><br>
  <b>8. Limitación de responsabilidad </b>
  <br><br>

    Ruslania no asumirá ninguna responsabilidad por los contenidos de 
    las publicaciones suscritas, ni por los cambios de número de páginas, 
    apariencia ni frecuencia de publicación.  Si una publicación deja de 
    editarse durante el período de  suscripción, Ruslania ofrecerá al 
    cliente en primer término otra publicación periódica, o en su defecto 
    cualquier otro producto vendido en su tienda por un valor equivalente 
    al dinero abonado por la suscripción.

 <br><br>
  <b>9. Fuerza mayor</b>
  <br><br>

    Ruslania no asumirá responsabilidades por envíos impedidos por 
    motivos de fuerza mayor (inconvenientes causados por fenómenos 
    naturales, catástrofes, huelgas, actos terroristas, sabotajes, 
    prohibiciones de importación/exportación, etc., o debido a otras 
    causas similares en las que la actuación de Ruslania o la de sus 
    asociados se vea imposibilitada).   En caso de suceder alguna de 
    estas situaciones, Ruslania procurará ponerse en contacto con el 
    cliente lo más pronto posible.

  <br><br>
  <b>10. Solución de controversias </b>
  <br><br>

    Las controversias y/o desavenencias en torno a las suscripciones o a 
    sus condiciones deberán resolverse, en primer término, mediante 
    negociaciones entre el cliente y Ruslania. De no alcanzarse un 
    acuerdo, el caso será sometido al Juzgado de Primera Instancia de 
    la ciudad de Helsinki.  La solución de controversias y/o 
    desavenencias surgidas de estas condiciones estará regida por 
    la legislación finlandesa.

