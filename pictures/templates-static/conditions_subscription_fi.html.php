   Ruslania Books Oy:n (josta j&auml;ljemp&auml;n&auml; k&auml;ytet&auml;&auml;n nime&auml; Ruslania) ja sen asiakkaiden v&auml;lisiin Internetin v&auml;lityksell&auml; tehtyihin lehtitilauksiin sovelletaan seuraavia ehtoja:
 
  <br><br>
  <b>1. Yleiset tilausehdot</b>
  <br><br>

  <b>1.1</b> Asiakas sitoutuu jokaisen tilauksen yhteydess&auml; kulloinkin voimassa oleviin sopimusehtoihin, jotka koskevat toimituksia Suomeen ja ulkomaille. Asiakkaan on ennen tilaamista tutustuttava voimassaoleviin tilausehtoihin ja hyv&auml;ksytt&auml;v&auml; ne. Ruslania pid&auml;tt&auml;&auml; oikeuden muuttaa toimitusehtojaan ilmoituksetta.

  <br><br>
 <b>1.2</b> Ruslania v&auml;litt&auml;&auml; m&auml;&auml;r&auml;aikais- ja kestotilauksia. Suomeen tilatut lehtitilaukset ovat yleens&auml; kestotilauksia ja ulkomaille m&auml;&auml;r&auml;aikaisia. Tilausjakson lopussa Ruslania l&auml;hett&auml;&auml; suomalaisille asiakkaille laskun uudesta jaksosta. Ulkomaalaisille asiakkaille l&auml;hetet&auml;&auml;n tarjous lehtitilauksen jatkosta. Huomatkaa mahdolliset hinnanmuutokset. Maksamalla laskun er&auml;p&auml;iv&auml;&auml;n menness&auml; asiakas varmistaa lehtitilauksensa katkeamattoman jatkumisen. Jos asiakas ei kuitenkaan halua jatkaa lehtitilausta, h&auml;n voi j&auml;tt&auml;&auml; laskun maksamatta. Ruslania kuitenkin pyyt&auml;&auml; t&auml;ss&auml; tapauksessa asiakasta ilmoittamaan asiasta asiakaspalveluun. 

  <br><br>
 <b>1.3</b> Mik&auml;li lehti ilmestyy v&auml;hemm&auml;n kuin 12 kertaa vuodessa, tilaus tehd&auml;&auml;n sille ajalle, joka on mahdollinen. Jos lehti ilmestyy esimerkiksi nelj&auml; kertaa vuodessa, tilaus voidaan tehd&auml; 3:lle (yksi numero), 6:lle (kaksi numeroa), 9:lle tai 12 kuukaudelle (4 numeroa). Mahdolliset tilausjaksot on esitetty nettisivuilla. V&auml;himm&auml;islaskutusyksikk&ouml; on kolme kuukautta.

 <br><br>
<b>2. Tilaaminen</b>
  <br><br>

  <b>2.1</b> Lehtitilauksen voi tehdä vain verkkomyymälään rekisteröinyt asiakas. Lehti toimitetaan asiakkaan tilauksessaan mainitsemaan osoitteeseen, mink&auml; oikeellisuudesta vastaa tilaaja. Ruslania ei vastaa toimitusvirheest&auml;, mik&auml;li se on ollut seurausta v&auml;&auml;r&auml;st&auml; osoitteesta. Lehden toimitusosoite voi olla my&ouml;s muu kuin maksajan osoite.
  <br><br>

  <b>2.2</b> Yleens&auml; lehtitilaukset alkavat tilausta seuraavan kuukauden ensimm&auml;isest&auml; p&auml;iv&auml;st&auml; tai ensimm&auml;isest&auml; mahdollisesta numerosta. Ruslania vahvistaa asiakkaalle lehtitilauksen alkamisajankohdan. Mahdolliset toivomukset asiakas voi esitt&auml;&auml; tilausta tehdess&auml;&auml;n vaiheessa 4 kohdassa  ”Toivomukset”.

  <br><br>
  <b>3. Muutokset</b>
  <br><br>

  <b>3.1</b> Kaikissa tilauksiin liittyviss&auml; asioissa, kuten nimen tai osoitteen muutoksissa, tulee ottaa yhteytt&auml; Ruslaniaan, eik&auml; esim. lehden kustantajaan tai v&auml;litt&auml;j&auml;&auml;n. Osoitemuutoksista on ilmoitettava mahdollisimman pian. Muutosten voimaantulo saattaa kest&auml;&auml; useiden numeroiden ajan. Ruslania vahvistaa saamansa osoitemuutoksen.

  <br><br>
  <b>4. Hinnat</b>
  <br><br> 
      Suomesta ulkomaille tilattujen lehtien hinnat m&auml;&auml;r&auml;ytyv&auml;t toimitusosoitteen mukaisesti. Yleens&auml; ulkomaille tilattuina lehdet ovat kalliimpia kuin Suomeen tilattuna. Pid&auml;t&auml;mme oikeuden hinnanmuutoksiin kustantajien hintojen tai valuuttakurssien muuttuessa. T&auml;m&auml; ei kuitenkaan koske jo tehtyj&auml; tilauksia.
