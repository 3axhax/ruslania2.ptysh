    CONSIGNES ET CONDITIONS RELATIVES AUX ABONNEMENTS
  </div>
  <br>

    Les abonnements contractés sur Internet entre la société Ruslania 
    Books Oy (ci-après Ruslania) et ses clients se verront appliquer 
    les conditions suivantes :
 <br><br>
  <b>1. Conditions générales des abonnements</b>
  <br><br>

    <b>1.1</b> 
    Ruslania ne vend et ne livre que des magazines 
    paraissant en Russie, dans l'ex-Union soviétique et en Finlande. 
    Le client s'engage pour chaque abonnement à respecter les conditions
     contractuelles en vigueur relatives aux livraisons en Finlande et à 
    l'étranger. Il devra avant de contracter l'abonnement 

     <br><br>

  <b>2. Abonnements</b>
  <br><br>

  <b>2.1</b>
    Il faut d'abord s'enregistrer comme client pour pouvoir prendre un 
    abonnement. Le magazine sera expédié à l'adresse mentionnée par le 
    client au moment de s'abonner. Ruslania ne saurait être tenue 
    responsable d'une erreur d'expédition due à une adresse incorrecte. 
    L'adresse à laquelle le magazine sera expédié pourra aussi être 
    différente de celle du payeur.

 <br><br>

  <b>2.2</b>
    D'une manière générale, les abonnements commencent à partir du premier 
    jour du mois suivant ou à partir du premier numéro paraissant. 
    Ruslania confirmera au client la date de commencement de l'abonnement. 
    Le client pourra faire part de ses éventuels souhaits au moment de 
    prendre l'abonnement, dans la phase 4 au point « Souhaits ».

  <br><br>

  <b>3. Modifications</b>
  <br><br>

  <b>3.1</b> 

    Dans toutes les questions liées aux abonnements, p. ex. dans les 
    changements de nom ou d'adresse, il conviendra de contacter Ruslania 
    et non l'éditeur du magazine ou l'intermédiaire. Les changements 
    d'adresse devront être notifiés au plus tôt. Il est possible que 
    plusieurs numéros soient envoyés avant que les modifications n'entrent 
    en vigueur. Ruslania confirmera les demandes de changements d'adresse 
    qui lui seront parvenues.

 <br><br>

  <b>4. Tarifs</b>
  <br><br>

  <b>4.1</b> 

    Ruslania ne vend que des abonnements à durée fixe et pour une durée 
    minimale d'un mois. Si le magazine paraît moins de 12 fois par an, 
    l'abonnement sera pris pour la période possible. S'il paraît par 
    exemple quatre fois par an, l'abonnement pourra être pris pour 3, 
    6, 9 ou 12 mois. Les périodes d'abonnement possibles figurent sur 
    les pages du site.
 <br><br>

  <b>4.2</b> Les tarifs des abonnements pris en Finlande, mais expédiés à 
    l'étranger, seront fonction de l'adresse de destination. D'une manière 
    générale, les magazines expédiés à l'étranger sont plus chers que s'ils
     sont expédiés en Finlande. Nous nous réservons le droit de procéder à 
    des ajustements tarifaires en cas de variation des tarifs des éditeurs 
    ou des taux de change. Cela ne concerne toutefois pas les abonnements 
    déjà contractés.
 <br><br>

  <b>5. Paiement de l'abonnement</b>
  <br><br>

  <b>5.1</b> 

    Les particuliers pourront payer leurs achats dans la boutique en 
    ligne à l'aide des services en ligne des banques ou de Luottokunta 
    (<a href="http://www.sampo.fi" class="maintxt">Sampo</a>, 
    <a href="http://www.nordea.fi" class="maintxt">Nordea</a>, 
    <a href="http://www.luottokunta.fi">Luottokunta</a>). Les entreprises paieront en général 
    sur facture. Dans le cas des entreprises situées en dehors de l'UE, 
    Ruslania se réservera le droit d'exiger un acompte.

  <br><br>

  <b>5.2</b> 

    Si l'adresse de facturation diffère de l'adresse d'expédition de 
    l'abonnement, le client devra le faire savoir au point « Souhaits » 
    (phase 4 lorsque l'on prend l'abonnement).

  <br><br>

  <b>5.3</b>

    Un particulier résidant en Finlande et n'ayant pas la possibilité 
    de payer via les banques en ligne pourra demander une facture à 
    notre service abonnements. Si le client a des factures échues non 
    réglées et qu'un échéancier de paiements n'a pas été convenu avec 
    notre service clients, Ruslania se réservera le droit de ne pas 
    traiter les nouvelles demandes d'abonnement avant le règlement
     des paiements dus.

  <br><br>

  <b>5.4</b> 

    Dans le cas des abonnements payés sur facture, le client devra faire 
    le règlement dans les quatorze (14) jours suivant la date de la
     facture, sauf si un autre délai de paiement aura été expressément 
    convenu. En cas de retard du règlement, les intérêts de retard prévus 
    par la loi relative aux intérêts (taux annuel : 11 %) seront 
    facturés ; après le deuxième rappel, des frais de rappels de 5 
    euros seront également facturés. Faute de réaction du client au 
    deuxième rappel, l'expédition des magazines sera suspendue et les 
    numéros déjà expédiés lui seront facturés.

 <br><br>

  <b>6. Observations et demandes de renseignements</b>
  <br><br>

  <b>6.1</b>

Nous vous prions de faire part de toutes les questions et observations 
relatives aux abonnements à l'adresse periodicals@ruslania.com. Nous 
vous prions de nous informer des éventuels numéros manquants dans les 
plus brefs délais, au plus tard à la réception du numéro suivant. 
Ruslania fera part dans la mesure du possible des retards et autres 
dans la parution des magazines.

  <br><br>
  <b>6.2</b> 

Les numéros non parus ou non expédiés en raison de problèmes techniques 
à l'impression ou autres seront compensés en prolongeant l'abonnement du 
client du nombre correspondant de numéros. Ruslania ne saurait être tenue
 responsable des dommages directs ou indirects résultant de l'état défectueux 
des produits ou d'un retard dans leur envoi.

  <br><br>
  <b>7. Annulation d'un abonnement</b>
  <br><br>

    Comme il s'agit de magazines expédiés en provenance de l'étranger, 
    les conditions habituelles de résiliation ne sont pas applicables
     aux abonnements pris chez Ruslania. L'abonnement ne pourra être 
    résilié qu'avec l'accord de l'éditeur. Si l'éditeur/l'intermédiaire 
    accepte la résiliation et rembourse le montant de l'abonnement 
    correspondant aux magazines non envoyés, le client pourra s'abonner 
    à un autre magazine ou commander d'autres produits de la librairie 
    pour le montant en question. Toutes les questions relatives aux 
    résiliations devront être envoyées à l'adresse : 
    periodicals@ruslania.com.

  <br><br>
  <b>8. Responsabilités </b>
  <br><br>

    Ruslania ne saurait être tenue responsable du contenu du magazine, 
    des modifications relatives au nombre de pages, de son apparence ou 
    de sa fréquence de parution. Si le magazine cesse de paraître en 
    cours d'abonnement, Ruslania cherchera en premier lieu à proposer 
    au client un autre magazine ou d'autres produits de la librairie 
    pour le montant acquitté.

  <br><br>
  <b>9. Force majeure </b>
  <br><br>

    Ruslania ne saurait être tenue responsable des magazines non 
    expédiés en cas de force majeure (catastrophes naturelles et autres, 
    grèves, actes terroristes, sabotage, interdiction d'exporter ou 
    d'importer, mesures des autorités et autres en conséquence de quoi 
    les activités de Ruslania ou de ses partenaires auront été 
    considérablement entravées). Si de telles circonstances survenaient, 
    les clients en seront informés dans les plus brefs délais.

  <br><br>
  <b>10. Règlement des litiges </b>
  <br><br>

    Les litiges et désaccords concernant les abonnements et leurs 
    conditions seront réglés en premier lieu par le biais de négociations 
    entre Ruslania et le client. S'il n'est pas possible de parvenir à un 
    accord, il sera possible de porter l'affaire devant le tribunal de 
    grande instance de Helsinki. La loi applicable au règlement des 
    litiges sera la loi finlandaise.

