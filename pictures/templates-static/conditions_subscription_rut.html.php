  USLOVIJA PODPISKI NA PERIODICHESKIE IZDANIJA
  </div>
  
  Ruslania Books Oy (v dal'nejshem Ruslanija) na svoem sajte v Internete proizvodit oformlenie podpiski na periodicheskie izdanija na sledujushchih uslovijah:

  <br><br>
  <b>1. Obshchie uslovija</b>
  <br><br>

  <b>1.1</b> Ruslanija oformljaet podpisku na izdanija, vyhodjashchie v Rossii, stranah byvshego SSSR i v Finljandii. V svjazi s kazhdym zakazom klient objazuetsja odobrit' i prinjat' dejstvujushchie uslovija podpiski v Finljandiju i drugie strany. Ruslanija ostavljaet za soboj pravo na izmenenie uslovij bez predvaritel'nogo objavlenija.

  <br><br>
  <b>2. Oformlenie podpiski</b>
  <br><br>

  <b>2.1</b> Podpiska oformljaetsja tol'ko dlja registrirovannyh pol'zovatelej. Izdanie budet dostavljat'sja po ukazannomu pri oformlenii zakaza adresu, za pravil'nost' kotorogo otvetstvennost' neset podpischik. Za izdanija, ne dostavlennye iz-za oshibki v adrese, Ruslanija otvetstvennosti ne neset.
  <br><br>

  <b>2.2</b> Podpiski obychno nachinajutsja s 1-go chisla sledujushchego mesjatsa ili s pervogo vozmozhnogo nomera. Ruslanija vsegda podtverzhdaet nachalo podpiski i srok ee dejstvija. Vozmozhnye v svjazi s podpiskoj pozhelanija nuzhno izlozhit' v punkte "Œoi pozhelanija" na 4-m shage oformlenija podpiski.

  <br><br>
  <b>3. Izmenenija dannyh</b>
  <br><br>

  <b>3.1</b> Po vsem voprosam, svjazannym s izmeneniem adresa ili imeni poluchatelja, sleduet obrashchat'sja tol'ko v Ruslaniju, a ne v redaktsiju ili k otpravitelju izdanija. Izmenenie adresa vhodit v silu cherez neskol'ko nomerov. Ruslanija vsegda podtverzhdaet prinjatie k svedeniju informatsii o takih izmenenijah.

  <br><br>
  <b>4. Tseny na izdanija</b>
  <br><br> 
  
  <b>4.1</b> Podpiska na periodiku oformljaetsja na opredelennyj srok i pri oplate edinitsej rascheta javljaetsja odin mesjats. Esli izdanie vyhodit rezhe 12 raz v god, to podpiska oformljaetsja sootvetstvenno na takoj srok, kotoryj vozmozhen dlja dannoj kratnosti vyhoda. Tak, naprimer, na izdanie, vyhodjashchee 4 raza v god, mozhet byt' oformlena podpiska na 3, 6, 9 ili 12 mesjatsev. Vozmozhnye sroki podpiski predstavleny na sajte. 
  <br><br>
  
  <b>4.2</b> Tseny opredeljajutsja stranoj dostavki: na nekotorye izdanija tseny dlja Finljandii nizhe, chem dlja drugih stran iz-za bolee nizkih pochtovyh tarifov. Ruslanija ostavljaet za soboj pravo na izmenenie tsen v svjazi s ih izmeneniem u postavshchikov, kolebanij valjutnyh kursov i t.p. Odnako na sdelannye i oplachennye zakazy tseny ne menjajutsja do okonchanija sroka podpiski i izmenenija kasajutsja tol'ko novyh zakazov.

  <br><br>
  <b>5. Oplata zakaza</b>
  <br><br>
  
  <b>5.1</b> Zakazy na periodiku oplachitvajutsja chastnymi litsami cherez bankovskie sistemy <a href="http://www.sampo.fi/english/" class="maintxt">Sampo</a>, <a href="http://www.nordea.fi/e/val-index.asp" class="maintxt">Nordea</a> ili kreditnymi kartami posredstvom kompanii <a href="http://www.luottokunta.fi/en/" class="maintxt">Luottokunta</a> (finskoj natsional'noj kompanii, obsluzhivajushchej kreditnye karty). Organizatsijam, kak pravilo, vystavljajutsja scheta, odnako ot organizatsij, nahodjashchihsja  vne ES, Ruslanija ostavljaet za soboj pravo trebovat' predoplatu zakaza. 
  <br><br>
  
  <b>5.2</b> V tom sluchae, esli schet dolzhen byt' poslan po drugomu adresu,  chem dostavka izdanija (napr., podarochnye podpiski), klient dolzhen eto objazatel'no ukazat' v punkte "Œoi pozhelanija" na 4-m shage oformlenija podpiski.
  <br><br>
  
  <b>5.3</b> Esli klient ne imeet vozmozhnosti oplatit' zakaz posredstvom sistem setevoj oplaty, to emu nuzhno obratit'sja v otdel periodiki s pros'boj vystavit' schet. Takaja vozmozhnost' predostavljaetsja tol'ko dlja zhitelej Finljandii. Ruslanija ostavljaet za soboj pravo otkazat' v oformlenii podpiski s vystavleniem scheta, esli u klienta imejutsja neoplachennye Ruslanii scheta.
  <br><br>
  
  <b>5.4</b> 4 Cchet dolzhen byt' oplachen v techenie 14 sutok s daty vystavlenija. Za prosrochku oplaty vzimaetsja penja v razmere 11% godovyh. Za vtorichnoe napominanie o neuplate vzimaetsja dopolnitel'no 5 evro. Esli zhe schet posle vtorichnogo napominanija ne oplachen, podpiska prekrashchaetsja i s klienta vzimaetsja plata za uzhe postavlennye nomera.
  
  <br><br>
  <b>6. Reklamatsii i spravki</b>
  <br><br>
  
  <b>6.1</b> Po vsem voprosam, svjazannym s periodikoj, prosim obrashchat'sja v otdel periodiki po ukazannym nizhe kontaktnym dannym. Reklamatsii v svjazi s nedostavlennymi nomerami sleduet posylat', esli, naprimer,  ezhemesjachnoe izdanie ne postupilo do kontsa mesjatsa. Reklamatsii prosim prisylat' po vozmozhnosti skoree, v pozdnem sluchae, srazu zhe posle postuplenija sledujushchego nomera. O vozmozhnyh zaderzhkah vyhoda izdanija ili ego dostavki Ruslanija bezotlagatel'no informiruet podpischikov.
  <br><br>
  
  <b>6.2</b> Esli ocherednoj nomer izdanija vyshel v nedostatochnom kolichestve ili s poligraficheskim brakom i etot nomer nevozmozhno postavit', srok podpiski budet sootvetstvenno udlinen. Ruslanija ne neset otvetstvennosti za prjamoj ili kosvennyj ushcherb, nanesennyj podpischiku nevyhodom ili nepostavkoj izdanija.
  
  <br><br>
  <b>7. Otkaz ot podpiski</b>
  <br><br>

  Tak kak Ruslanija postavljaet, v osnovnom, vyhodjashchie  za predelami Finljandii izdanija, dejstvujushchie vnutri strany uslovija otkaza ot podpiski ne primenjajutsja. Otkazat'sja ot podpiski mozhno tol'ko v tom sluchae, kogda izdatel' ili upolnomochennyj rasprostranitel' odobrjaet otkaz i vozvrashchaet Ruslanii uplachennuju za podpisku summu. Šlient mozhet na etu summu libo oformit' podpisku na drugoe izdanie, libo priobresti druguju produktsiju, predstavlennuju na sajte. Po vsem voprosam, svjazannym s otkazami ot podpiski, sleduet obrashchat'sja v otdel periodiki po ukazannym nizhe dannym.
  
  <br><br>
  <b>8. Otvetstvennost'</b>
  <br><br>

  Ruslanija ne neset otvetstvennosti za soderzhanie izdanij, izmenenija obema, vneshnego vida i kratnosti vyhoda. Esli vyhod izdanija prekrashchaetsja ili ono zakryvaetsja, to podpischiku predlagaetsja libo drugoe izdanie, libo ljubaja drugaja produktsija, predstavlennaja na sajte.

  <br><br>
  <b>9. Obstojatel'stva nepreodolimoj sily (force majeure)</b>
  <br><br>

  Ruslanija ne neset otvetstvennosti za nepostavku izdanij vvidu dejstvija obstojatel'stv nepreodolimoj sily, takih kak stihijnye bedstvija, katastrofy, zabastovki, terroristicheskie akty, sabotazh, zapreshchenie eksporta ili importa, dejstvija organov vlasti i drugih analogichnyh obstojatel'stv, vvidu kotoryh dejatel'nost' Ruslanii ili ee partnerov sushchestvenno zatrudnjaetsja. O nastuplenii takih obstojatel'stv podpischikam soobshchaetsja nezamedlitel'no.

  <br><br>
  <b>10. Reshenie sporov</b>
  <br><br>

  Voznikshie v svjazi s etimi uslovijami podpiski na periodicheskie izdanija spory i raznoglasija reshajutsja, v pervuju ochered', mezhdu Ruslaniej i klientom. Esli zhe dostizhenie dogovorennosti nevozmozhno, to spory reshajutsja v gorodskom sude g. Hel'sinki s primeneniem zakonodatel'stva Finljandii.
