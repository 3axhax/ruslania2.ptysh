<h4>Ruslania</h4>
<p>The bookstore was founded in Helsinki 1986. Ruslania Books Oy is a 100% Finnish family business that imports Russian books, movies, music, and magazines. We also offer Russian and Finnish souvenirs, calendars and quality Russian food and drinks. Our webstore selection also has Estonian books.</p>
 
<h4>Idea</h4>

<p>Ruslania's business idea is that we keep in our catalogs all books published in Finland that are somehow related to Russia, both fiction and non-fiction. The store has the importer's prices. We also offer our products to libraries and bookshops.</p>
 
<h4>Team</h4>
 
<p>The staff will help you find suitable products, whether it is for gift giving or searching for a particular product from Russia or Finland. We also invite teachers to choose new teaching material and we welcome student groups to a study visit!</p>
 
