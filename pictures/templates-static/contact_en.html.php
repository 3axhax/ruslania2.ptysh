<br><br>
The products you want are waiting for you<br>

You can place your order and choose the delivery method "fetch the order at the store"! In this way, you will be assured that the products you order are already waiting for you when you arrive at the store - however, note that the order is handled among other online orders and may not be available for pickup during the same day!

We will send a notification upon completion of the order via email, SMS or call. Pickup orders are usually ready for pick up within 2-7 business days. We keep the pick up orders for a week from the order date, after which the order is dismantled and returned to the warehouse.
