          <div style="font-size: 120%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">
            Pererabotka
          </div>

          Odin iz kljuchevyh aspektov – ostavljat' men'she mesta. «Ruslanija» pererabatyvaet vsju bumagu, plastik, steklo, metall, batarejki i karton. Takzhe othody, nahodjashchiesja doma, sortirujutsja otdel'no.
          Bolee 85% vseh kartonnyh paketov, v kotorye upakovana produktsija magazina budut pererabotany i ispol'zovany zanovo. Takim obrazom, my hotim dobit'sja bol'shih uspehov v bezothodnom proizvodstve i
          sohranit' derev'ja, kotorye vyrubajutsja dlja sozdanija kartona. «Ruslanija» ispol'zuet ochen' malo beloj bumazhnoj produktsii po sravneniju s ego konkurentami. Vsju beluju bumagu my ispol'zuem dvazhdy,
          ispol'zuja chistuju storonu. Osoboe vnimanie udeljaetsja uluchsheniju effektivnosti protsessu upakovki i elektronnym sredstvam, eto otnositsja kak k vhodjashchim, tak i ishodjashchim postavkam produktsii.
          Мy ispol'zuem polotentsa dlja ruk vmesto salfetok, personal nikogda ne ispol'zuet odnorazovuju posudu.
          <br>
            <br>

              <div style="font-size: 120%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">
                Мen'she othodov
              </div>
              Мy staraemsja ne pokupat' upakovannye tovary. Мy importiruem produktsiju iz Rossii, snachala tovar popadaet na nash sklad v Sankt-Peterburge, zatem transportirujutsja v Hel'sinki. Takim obrazom, my
              mozhem prosit' postavshchika ne upakovyvat' tovary, kak by oni upakovali ih dlja mezhdunarodnyh perevozok. «Мen'she othodov» - eta ideja otrazhaetsja i na nashih postavkah, kogda my otpravljaem zakazy klientam.
              Мy upakovyvaem akkuratno, no ne zavorachivaem vse v plastik. Vmesto etogo my ispol'zuem korichnevuju bumagu, kotoraja postavljaetsja s knigami iz Rossii, i gazety, kotorye ne prodalis' v nashem magazine.

              <br>
                <br>

                  <div style="font-size: 120%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">
                    Мen'she zagrjaznenija
                  </div>
                  Vse knigi, kotorye my pokupaem u nashih postavshchikov v Hel'sinki, my zabiraem lichno: na velosipede ili peshkom. Мnogie iz nashih kljuchevyh postavshchikov raspolozheny v Stolichnoj zone, i my mozhem zabrat'
                  mnogie tovary bez ispol'zovanija transportnyh sredstv, zagrjaznjajushchih okruzhajushchuju sredu.<br>
                    <br>

                      Dostavka iz Rossii formiruetsja takim obrazom, chtoby za odin raz privezti maksimal'noe kolichestvo tovara, tem samym sniziv zatraty na toplivo i zagrjaznenie okruzhajushchej sredy.<br>
                        <br>

                          Мy arenduem tol'ko odno parkovochnoe mesto okolo nashih pomeshchenij v Hel'sinki. Мy pooshchrjaem nashih sotrudnikov, ispol'zujushchih obshchestvennyj transport, velosipedy ili prosto prihodjashchih peshkom.
                          Nashi mesta vsegda byli vybrany tak, chtoby do nih bylo legko dobrat'sja na avtobuse, tramvae, poezde ili metro. Rabotniki, priezzhajushchie na velosipedah, mogut vospol'zovat'sja dushevymi komnatami, kotorymi osnashcheny nashi rabochie mesta.<br>
