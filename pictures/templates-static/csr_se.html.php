      <div style="font-size: 130%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">
        Corporate citizenship CSR program of Ruslania Books Corp.
      </div>
      <div style="font-size: 120%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">
        Recycling
      </div>

      This is one of the key aspects of leaving a smaller footprint. Ruslania recycles all paper, plastic, glass, metal, batteries and cardboard. Also house hold wastes are sorted separately. More than 85 % of all cardboard packages that reach Ruslania will be used again and sent to our customers. In this manner we succeed in creating less waste and saving trees from being cut to create cardboard. Ruslania uses very little white copy paper compared to its peers. We use again all white paper, which is printed on one side - some times even twice. Special attention is paid to enhancing the efficiency of packaging and electronic tools are put to extensive use in both incoming product deliveries and outgoing order processing. We use mostly hand towels instead of tissue papers. In the kitchen the personnel never uses disposable dishes.

      <br>
        <br>

          <div style="font-size: 120%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">Less waste</div>

          We try not to buy over packaged goods. We import products from Russia so that they are first compiled in our warehouse in St. Petersburg, then transported in a van to Helsinki. In this manner, we can ask suppliers not to pack the goods as they would pack them for an international transport. The "less waste" idea also goes to shipments that we send to our customers. We try to pack carefully, but not wrap everything in plastic. Instead, we use brown paper that comes with books from Russia, and newspapers that are left unsold at the shop.

          <br>
            <br>

              <div style="font-size: 120%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">Less pollution</div>
              All the books that we buy from our suppliers in Helsinki, we pick up with our own transit: bike or walking. Many of our key suppliers are situated in the Greater Helsinki Area,
              and we are able to pick up many products without using any combustion motor vehicle.<br>
                <br>
                  Our import deliveries from Russia are always brought in fully and optimally packed vans. This saves petrol and creates less pollution.<br>
                    <br>
                      In our premises in Helsinki, we rent only one parking lot for a car. We encourage our staff to walk, drive by bike, run or use public transit. Our
                      locations have always been chosen so that it's easy to come with bus, tram, train or metro. Our work place also has possibilities to wash up after sports or bike drive to work.<br>
                        <br>


                          <div style="font-size: 120%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">
                            Taking care of personnel
                          </div>
                          All staff members are provided with a full coverage ticket to public transport in Helsinki. The company participates in the staff members' spending on sport. All employees can
                          attend self development courses, mainly to improve their language skills, and the company pays for the courses. All employees are always welcome to give their ideas and to say their opinions out loud.<br>
                            <br>
                              The management of the company has promised to be there if a member of the staff asks any help with issues either in work or in private life.<br>
                                <br>

                                  <div style="font-size: 120%; font-weight: bold; margin-top: 5px; margin-bottom: 10px">
                                    No explicit material
                                  </div>
                                  Another aspect of our CSR program is that Ruslania doesn't sell porn. We feel the responsibility for protecting children and youth from harmful visual material and we do our best to limit their access to explicit material. There are no x-rated products sold in any of our departments: magazines, DVDs, software. There are also no porn advertising on our webstore.
