<DIV><I><FONT color=#0000ff>Kann man sich die Kategorien des Onlinegeschäfts ohne Anmeldung anschauen?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Man kann, aber ohne Anmeldung kann man keine Bestellung durchführen. Die Waren- und Versandpreise 
werden nicht unbedingt Ihrer Region entsprechen. Für die Kunden aus den EU-Ländern enthalten die Preise 
die Mehrwertsteuer, und für die Kunden aus anderen Ländern werden die Preise ohne Mehrwertsteuer angezeigt.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Warum kann ich mich nicht anmelden, wenn ich die E-Mailadresse und Passwort 
eingegeben habe?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Wahrscheinlich, weil Sie sich noch nicht als Neukunde angemeldet haben. In diesem Fall sollen Sie sich 
anmelden. Wenn Sie schon angemeldet sind, aber die Mitteilung "der Benutzername oder das Passwort sind falsch" 
erscheint, wurden die E-Mail-Adresse oder das Passwort fehlerhaft eingegeben.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Kann man mit der Kreditkarte einer anderen Person 
(des Ehemannes, der Ehefrau, eines Bekannten usw.) bezahlen?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Leider darf man das nicht, weil die Zahlung nach dem Namen des Besitzers der Kreditkarte läuft, also der 
Kunde und der Besitzer der Kreditkarte sollte eine und dieselbe Person sein. Wenn Sie mit der Kreditkarte einer 
anderen Person bezahlen wollen, dann melden Sie diese Person an und führen Sie die Bestellung unter dem Namen 
des Kartenbesitzers durch.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Wie sicher ist bei Ruslania die Kreditkartenbearbeitung?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Ruslania führt keine Kreditkartenbearbeitung durch und hat keinen Zugang zu den Kreditkarten. Wenn der 
Kunde den Einkauf mit der Kreditkarte bezahlt, so wird der Kunde nach der Bestellung auf die Webseite der 
zusatändigen Organisation weitergeleitet, die solche Zahlungen bearbeitet. In Finnland heißt diese 
Organisation Paytrail. Nach der Bearbeitung der Karte wird das System mitteilen, ob Zahlungstransfer 
erfolgreich war. Danach kann der Kunde auf Ruslania weiter einkaufen oder den Vorgang beenden.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Warum wird die Kreditkarte nicht akzeptiert, obwohl die Kreditkarte gültig ist 
und der Kontostand in Ordnung ist? </FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Wahrscheinlich, weil Ihre Karte für das Einkaufen im Internet nicht zugelassen ist. Fragen Sie danach 
bei Ihrer Bank. Wenn alles in Ordnung ist und der Zahlungstransfer trotzdem nicht erfolgreich ist, teilen 
Sie dies bitte uns mit.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Kann eine andere Person sich meine Bestellungen ansehen?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Nein, es ist nicht möglich, wenn Sie niemandem Ihr Passwort anvertraut haben. Wenn jemand Ihre E-Mail 
und Ihr Passwort kennt, kann er sich die Bestellungen ansehen.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Wie oft werden Bestellungen und Anfragen angenommen?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Werktags die ganze Zeit. Die Bestellungen, die samstags oder sonntags abgeschlossen werden, 
werden montags bearbeitet.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Wovon hängt die Größe der Versandgebühr ab?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Nach den Regeln der finnischen Post ist die Versandgebühr von dem Gewicht und der Dicke der Sendung 
abhängig. Wenn das Gewicht mehr als 1 kg ist, oder die Dicke der Sendung 3 cm übersteigt, kostet der Versand 
7 Euro, wenn nicht – 3.90 Euro. Für die Kunden, die außerhalb Finnlands wohnen, gelten andere Versandgebühren, 
die Sie bei der Bestellung erfahren werden.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Warum wird bei der Bestellung unter 5 Euro (z.B. eine Postkarte) der Preis 
automatisch auf 5 Euro erhöht?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Das passiert, weil der Mindestbestellwert bei Ruslania 5 Euro ist.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Wie oft wird die Web-Site und die Warenanzahl erneuert?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Unsere Web-Site wird gewöhnlich 1-2 Mal pro Tag erneuert, die Warenanzahl wird automatisch jede halbe 
Stunde erneuert.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Warum ändert sich die Anzahl nach dem Drücken auf "Warenkorb neu berechnen"?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Weil das Programm die zum Zeitpunkt der Bestellung vorhandene Anzahl eines Artikels überprüft. Wenn Sie 
z.B. 3 gleiche Postkarten kaufen wollen, und nach dem Drücken auf "Warenkorb neu berechnen" bei "Anzahl" nur 
"1" steht, bedeutet das, dass nur 1 Exemplar vorhanden ist.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Wo ist der Unterschied zwischen "Nicht vorhanden" und "Die Ware zur Zeit nicht 
vorhanden"?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>"Die Ware zur Zeit nicht vorhanden" bedeutet, dass die Ware zur Zeit nicht vorhanden ist, ihre Lieferung 
aber bald erwartet wird.<br>
"Nicht vorhanden" bedeutet, dass die Ware entweder bei den Lieferanten ausgegangen ist, oder nicht mehr 
gedruckt wird, und wir in der nächsten Zeit diese Ware nicht liefern können. Sie können eine Anfrage 
hinterlassen. 
</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Ich habe die Ware online bestellt. Warum wird sie nicht sofort abgeschickt?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Wir haben außer dem Onlinegeschäft auch ein Geschäft im Zentrum von Helsinki. Leider kann es passieren, 
dass die Kunden im Geschäft das letzte Exemplar der von Ihnen bestellten Ware gekauft haben, und wir an die 
nächste Lieferung angewiesen sind. In solchem Fall informieren wir den Kunden über die Verspätung.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>In welchem Fall ist die Rückgabe der Ware möglich?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Die Rückgabe der Ware ist nach geltendem Recht zu handhaben. Rückgaben sind stets im Voraus mit unserer 
Kundenabteilung abzusprechen (siehe <a href="http://www.ruslania.com/context-296/entity-1/language-5.html">
Kontakt</a>). Das Rückgaberecht besteht nicht für CDs, DVDs, Audio- und 
Videokassetten und Software, die vom Kunden entsiegelt wurde, sowie nicht bei Leistungen, die Online 
(z.B. Download-Software) ausgeführt wurden. Wenn die Rückgabe der Ware aus dem Ausland (außerhalb Finnlands) 
erfolgt, trägt der Kunde die Versandkosten der Rücksendung. </DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Was ist "Zone 5" und wie sieht man sie sich an?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Alle DVD-Filme werden mit Regionalcode des Landes versehen, wo sie verbreitet werden. Die Welt ist 
auf 6 Zonen aufgeteilt. Russland gehört zur Zone 6, die USA und Kanada - zur Zone 5, Europa – zur Zone 2. 
Deshalb funktionieren die DVD-Filme einer Zone nicht auf den DVD-Spielern der anderen Zone. <br>
Es gibt einige Varianten der Abspielung solcher DVD:
<ul type=disc>
<li>Kauf eines Multizonenspielers (fragen Sie im Geschäft nach)</li>
<li>Kauf eines DVD-Spielers in Russland</li>
<li>Abspielung des Filmes auf dem Computer</li>
<li>Entcodierung des vorhandenen DVD-Spielers</li>
</ul>
</DIV>

