<DIV><I><FONT color=#0000ff>Mozhno li smotret’ razdely sajta bez registratsii?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Da, mozhno, tol’ko nel’zja budet oformit’ zakaz ili ostavit’ zajavku. K tomu zhe, tseny na produktsiju i 
peresylku ne objazatel’no budut sootvetstvovat’ Vashemu regionu. Dlja pol’zovatelej iz stran ES tseny 
pokazyvajutsja s uchetom NDS (=Nalog na dobavlennuju stoimost’, VAT), a dlja pol’zovatelej iz drugih stran – 
bez etogo naloga.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Pochemu, vvedja elektronnuju pochtu i parol’, ja ne mogu popast’ na sajt?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Skoree vsego potomu, chto Vy eshche ne zaregistrirovalis’. V etom sluchae nuzhno zaregistrirovat’sja, 
posle chego vhod na sajt stanet dostupnym. Esli zhe Vy zaregistrirovany, no pojavljaetsja soobshchenie 
«Vvedennoe imja pol’zovatelja ili parol’ neverny», to, po vsej vidimosti, e-majl i parol’ byli vvedeny s 
oshibkami.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Mozhno li platit’ chuzhoj kreditnoj kartoj (muzha, zheny, znakomogo i t.d.)?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>K sozhaleniju, etogo delat’ nel’zja, t.k. platezh projdet po imeni vladel’tsa kreditnoj karty, to est’ pol’zovatel’ i vladelets dolzhny byt’ odno i to zhe litso. Esli Vy hotite sdelat’ oplatu chuzhoj kartoj, to nuzhno zaregistrirovat’ ee vladel’tsa i sdelat’ zakaz ot ego imeni.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Naskol’ko nadezhno v Ruslanii zashchishchena obrabotka kreditnyh kart?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Kreditnye karty v Ruslanii ne obrabatyvajutsja i nikakogo dostupa k kartam Ruslanija ne imeet. Esli klient oplachivaet pokupku kreditnoj kartoj, to posle oformlenija zakaza on pereadresovyvaetsja na sajt upolnomochennoj organizatsii, kotoraja i obrabatyvaet podobnye platezhi. V Finljandii takoj organizatsiej javljaetsja Luottokunta. Posle obrabotki karty sistema soobshchit o prohozhdenii (ili neprohozhdenii) platezha. Zatem pol’zovatel’ mozhet libo vernut’sja na sajt Ruslanii, libo zakonchit’ sessiju.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Pochemu platezh kreditnoj kartoj ne prinimaetsja, hotja karta  v porjadke i sredstva na schetu tochno imejutsja?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Vozmozhno, chto v Vashej karte net optsii na oplatu pokupok v seti Internet. Svjazhites’ s bankom, vydavshem kreditnuju kartu,  i uznajte, tak li eto. Esli vse v porjadke, no platezhi, tem ne menee, ne prohodjat, pozhalujsta, soobshchite nam ob etom.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Mozhet li postoronnee litso smotret’ sdelannye mnoju zakazy?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Net, eto nevozmozhno, esli Vy nikomu ne soobshchaete svoj parol’. Esli zhe kto- to znaet Vash majl i parol’, to on smozhet uvidet’ i istoriju.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Kak chasto prinimajutsja zakazy i zajavki?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Po rabochim dnjam postojanno, a zakazy, poluchennye v subbotu ili voskresen’e obrabatyvajutsja v ponedel’nik.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Ot chego zavisit razmer pochtovyh rashodov?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Po pravilam finskoj pochty, na tsenu peresylki vlijajut ves i tolshchina peresylaemogo tovara. Esli ves prevyshaet 1 kg, libo tolshchina paketa prevyshaet 3 sm, to pochtovye rashody sostavjat 5 evro, v protivnom sluchae – 2.90 evro. Dlja klientov, zhivushchih vne Finljandii dejstvujut inye rastsenki, kotorye vy mozhete uvidet’ vo vremja oformlenija zakaza.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Pochemu pri zakaze men’she, chem na pjat’ evro (napr. odna otkrytka), programma povyshaet stoimost’ do pjati evro?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Eto proishodit potomu, chto summa minimal’noj pokupki v virtual’nom magazine Ruslanii sostavljaet pjat’ evro.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Kak chasto obnovljaetsja sajt i kolichestvo tovara?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Nash sajt obychno obnovljaetsja 1-2 raza v den’, kolichestvo tovara obnovljaetsja avtomaticheski kazhdye polchasa.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Pochemu posle shchelchka po knopke «Obnovit’ korzinu» menjaetsja vvedennoe kolichestvo?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Prichina v tom, chto programma, prezhde chem rasschitat’ stoimost’ tovara, proverjaet dostupnoe na moment zakaza kolichestvo odnogo naimenovanija. Esli, naprimer, Vy hotite kupit’ 3 odinakovyh otkrytki, a posle pererascheta v grafe «kolichestvo» pokazyvaetsja tol’ko «1», to eto znachit, chto v nalichii imeetsja tol’ko 1 ekzempljar.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Kakaja raznitsa mezhdu «Net v nalichii» i «Tovar vremenno zakonchilsja»?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Nadpis’ «Tovar vremenno zakonchilsja» oboznachaet, chto na dannyj moment tovara net, no my zhdem postavku v blizhajshee vremja.
«Net v nalichii» oboznachaet, chto libo tovar zakonchilsja u postavshchikov, libo voobshche zakonchilsja tirazh i v blizhajshee vremja my etot tovar poluchit’ ne smozhem. Vy mozhete ostavit’ zajavku.
</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Ja zakazal tovar na sajte. Pochemu mne ne vysylajut ego srazu zhe?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>U nas pomimo internet-magazina est’ eshche obychnyj magazin v tsentre Helsinki. K sozhaleniju, inogda sluchaetsja tak, chto klient v magazine uspevaet kupit’ poslednij ekzempljar tovara, poetomu nam prihoditsja zhdat’ druguju postavku. My objazatel’no informiruem klienta o zaderzhke.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>V kakom sluchae vozmozhen vozvrat tovara?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Vozvrat produktsii reguliruetsja sushchestvujushchim zakonodatel’stvom. Prezhde, chem vozvratit’ produktsiju, klient dolzhen svjazat’sja s otdelom obsluzhivanija (sm. <a href="http://www.ruslania.com/context-296/entity-1/language-3.html">Kontakty</a>). Pravo vozvrata ili obmena ne rasprostranjaetsja na audio- i videokassety, CD, DVD i programmnye produkty, esli ih original’naja upakovka byla narushena zakazchikom. Pravo vozvrata ne rasprostranjaetsja na programmy, poluchennye posredstvom Interneta («skachivaemye» programmy). Esli produktsija vozvrashchaetsja izvne Finljandii, to klient dolzhen oplachivat’ pochtovye rashody (krome vozvrata brakovannoj produktsii).</DIV>

