<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Nuzhno li oplachivat’ tamozhennuju poshlinu ili nalogi?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Esli Vy izvne ES, to vozmozhno, chto Vam pridetsja zaplatit’ NDS v svoej strane pri poluchenii paketa. Za dopolnitel’noj informatsiej sleduet obrashchat’sja k mestnym tamozhennym organam.</DIV>

<DIV>&nbsp;</DIV>

<DIV><I><FONT color=#0000ff>Chto delat’, esli programma soobshchaet, chto pol’zovatel’ s takim elektronnym adresom uzhe zaregistrirovan?</FONT></I></DIV>
<DIV>&nbsp;</DIV>
<DIV>Eto proishodit v tom sluchae, esli Vy uzhe odnazhdy zaregistrirovali svoj elektronnyj adres i pytaetes’ zaregistrirovat’sja zanovo, naprimer, esli Vy kogda-to uzhe zaregistrirovalis’, no zabyli ob etom, ili ne mozhete vojti iz-za nevernogo parolja i pytaetes’ snova projti registratsiju. Takzhe takoe byvaet, esli hotite zaregistrirovat’ drugogo cheloveka pod svoim adresom, naprimer, dlja oplaty kreditkoj. Ili hotite zaregistrirovat’sja s takim zhe elektronnym adresom ot imeni organizatsii.</DIV>

