<DIV><I><FONT color=#0000ff>What is "zone 5" and how one can watch DVDs that carry that zone?</FONT></I></DIV>
<DIV><I></I>&nbsp;</DIV>
<DIV>All DVD films are sealed with regional code of the country where they are spread. The world is split to 6 zones. Russia belongs to the zone 6, the USA and Canada - to the zone 5, Europe – to the zone 2. Therefore, there do not function the DVD films of a zone on the DVD players of the other zone.<br/>
There are some ways to watch such DVDs:
<ul>
    <li>Purchase of a multi zone player (you can ask Ruslania for a one)</li>
    <li>Purchase of a DVD player in Russia</li>
    <li>Watch of the film on the computer</li>
    <li>Decode your DVD player</li>
</ul>
</DIV>
