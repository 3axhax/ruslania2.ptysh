<p>
Sustainability programs and corporate citizenship declarations are a very
welcome phenomenon. The bigger the company grows, the easier it is to forget
about the effects it has on the environment.</p>
 
<h2>Corporate citizenship CSR program of Ruslania Books Corporation.</h2>
 
 
<h2>Recycling</h2>

<p>This is one of the key aspects of leaving a smaller footprint. Ruslania
recycles all paper, plastic, glass, metal, batteries and cardboard. Also house hold wastes are sorted separately. More than 85 % of all cardboard packages that reach Ruslania will be used again
and sent to our customers. In this manner we succeed in creating less waste
and saving trees from being cut to create cardboard. Ruslania uses very
little white copy paper compared to its peers. We use again all white paper, which is printed on one side – some times even twice. Special attention is paid to enhancing
the efficiency of packaging and electronic tools are put to extensive use in
both incoming product deliveries and outgoing order processing. We use mostly hand towels instead of tissue papers. In the kitchen the personnel never uses disposable dishes.</p>
 
<h2>Less waste</h2>
<p>We try not to buy over packaged goods. We import products from Russia so
that they are first compiled in our warehouse in St. Petersburg, then
transported in a van to Helsinki. In this manner, we can ask suppliers not
to pack the goods as they would pack them for an international transport.
The “less waste” idea also goes to shipments that we send to our customers.
We try to pack carefully, but not wrap everything in plastic. Instead, we
use brown paper that comes with books from Russia, and newspapers that are
not sold at our shop.</p>
 
<h2>Less pollution</h2>
<p>We buy all books that are available in Helsinki with our own transit: bike
or walking. Many of our key suppliers are situated in the Greater Helsinki
Area, and we are able to pick up many products without using any combustion
motor vehicle.</p>
 
<p>Our import deliveries from Russia are always brought in fully and optimally
packed vans. This saves petrol and creates less pollution.</p>
 
<p>In our premises in Helsinki, we rent only one parking lot for a car. We
encourage our staff to walk, drive by bike, run or use public transit. Our
locations have always been chosen so that it’s easy to come with bus, tram, train or metro. Our work place also has possibilities to
wash up after sports or bike drive to work.</p>
 
<p>The company participates in the staff members’ spending on sport.
All employees are always welcome to give their ideas and to say their
opinions out loud.</p>
 
<p>The management of the company has promised to be there if a member of
the staff asks any help with issues either in work or in private life.
All employees can attend to self development courses, mainly to improve
their language skills, and the company pays for the courses.</p>
 
<h2>No explicit material</h2>

<p>Another aspect of our CSR program is that Ruslania doesn’t sell porn. We
feel the responsibility for protecting children and youth from harmful
visual material and we do our best to limit their access to explicit
material. There are no x-rated products sold in any of our departments:
magazines, DVDs, software. There are also no porn advertising on our
webstore.</p>
