<h2>Kierr&auml;tt&auml;minen</h2>
<p>Kierr&auml;tt&auml;minen on yksi t&auml;rkeimmist&auml; keinoista vaikuttaa hiilijalanj&auml;ljen suuruuteen. Ruslania kierr&auml;tt&auml;&auml; kaikki paperit, muovit, lasit, 
metallit, patterit ja pahvit. My&ouml;s bioj&auml;tteet lajitellaan erikseen. Yli 85% kaikista Ruslaniaan tulleista pahvilaatikoista k&auml;ytet&auml;&auml;n uudelleen 
ja l&auml;het&auml;&auml;n asiakkaillemme. N&auml;in onnistumme v&auml;hent&auml;m&auml;&auml;n j&auml;tteenkulutusta ja suojelemaan puita. Ruslania k&auml;ytt&auml;&auml; tarpeisiinsa n&auml;hden huomattavan 
v&auml;h&auml;n valkoista kopiopaperia. K&auml;yt&auml;mme uudelleen – joskus jopa kaksi kertaa – kaikki kopiopaperit, joihin on tulostettu tai kopioitu vain toiselle
 puolelle. Ty&ouml;paikallamme k&auml;yt&auml;mme suurimmaksi osaksi kankaisia pyyhkeit&auml; k&auml;sipapereiden sijaan. Keitti&ouml;ss&auml; emme koskaan k&auml;yt&auml; kertak&auml;ytt&ouml;isi&auml; ruokailuv&auml;lineit&auml;.</p>

<h2>V&auml;hemm&auml;n j&auml;tett&auml;</h2>

<p>Yrit&auml;mme v&auml;ltt&auml;&auml; ylipakattuja tuotteita. Maahantuomme Ven&auml;j&auml;lt&auml; tuotteita, jotka on pakattu varastossamme Pietarissa ja sitten kuljetettu pakettiautossa Helsinkiin. 
T&auml;ll&auml; tavoin voimme pyyt&auml;&auml; toimittajiamme, etteiv&auml;t he ylipakkaisi tuotteita kuten he normaalisti pakkaisivat kansainv&auml;list&auml; kuljetusta varten. Yrit&auml;mme pakata huolellisesti 
k&auml;ytt&auml;m&auml;tt&auml; kuitenkaan paljon muovia. Sen sijaan k&auml;yt&auml;mme ruskeaa paperia, jota saamme kirjojen pakkausmateriaalina Ven&auml;j&auml;lt&auml;, sek&auml; vanhoja sanomalehti&auml;, 
joita ei ole myym&auml;l&auml;ss&auml;mme myyty. </p>

<h2>V&auml;hemm&auml;n saastetta</h2>
<p>Kirjat, jotka Ruslania ostaa kustantajilta Helsingiss&auml;, kuljetamme itse joko py&ouml;r&auml;ll&auml; tai k&auml;vellen. Monet toimittajamme sijaitsevat Helsingin ydinkeskustassa ja meill&auml; on 
mahdollisuus hakea tuotteita k&auml;ytt&auml;m&auml;tt&auml; ymp&auml;rist&ouml;&auml; saastuttavia kulkuneuvoja. </p>

<p>Kaikki l&auml;hetyksemme Ven&auml;j&auml;lt&auml; tuodaan pakettiautolla, joka on aina mahdollisimman t&auml;yteen pakattu. N&auml;in s&auml;&auml;st&auml;mme bensaa sek&auml; v&auml;henn&auml;mme saasteita.<p>

<p>Yrityksell&auml;mme on toimitilojen yhteydess&auml; vain yksi autopaikka vuokrattuna. Kannustamme henkil&ouml;kuntaamme k&auml;ytt&auml;m&auml;&auml;n julkisia liikennev&auml;lineit&auml;, py&ouml;r&auml;ilem&auml;&auml;n, 
