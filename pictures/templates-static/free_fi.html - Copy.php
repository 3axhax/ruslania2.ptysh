Ty&ouml;paikallamme on my&ouml;s mahdollisuus peseyty&auml; esimerkiksi urheilun tai py&ouml;r&auml;ilemisen j&auml;lkeen. </p>

<p>Yrityksemme osallistuu henkil&ouml;kunnan liikuntakulujen korvaamiseen. Vuonna 2011 on tarkoitus alkaa korvata kuluja my&ouml;s julkisen liikenteen k&auml;yt&ouml;st&auml;. 
Jokaisen ty&ouml;ntekij&auml;n ideat ja mielipiteet kuunnellaan. </p>

<p>Yrityksen johto on aina valmis auttamaan ty&ouml;ntekij&ouml;it&auml;&auml;n niin ty&ouml;- kuin yksityisel&auml;m&auml;nkin ongelmissa. Kaikkia ty&ouml;ntekij&ouml;it&auml; kannustetaan k&auml;ym&auml;&auml;n 
