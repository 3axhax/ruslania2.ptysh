<h3>Rekisterin suojauksen periaatteet</h3>
<p>A. Manuaalinen aineisto (säilytyspaikka ja suojaaminen)</p>
<p>Korttihakemukset ja muut manuaalisesti käsiteltävät asiakastietoja sisältävät asiakirjat säilytetään alkukäsittelyn jälkeen lukituissa ja paloturvallisissa säilytystiloissa. Ainoastaan Ruslania Books Oy:n työntekijöillä, Onnimaxin asiamiehillä sekä Ruslania Books Oy:n toimeksiannosta ja lukuun toimivien yritysten työntekijöillä on oikeus käsitellä manuaalisesti tallennettuja asiakastietoja.</p>
<p>B. ATK:lle talletetut tiedot (rekisterin käyttöoikeuden periaatteet ja käytön valvonta sekä laitteiden fyysinen suojaus)</p>
<p>Ainoastaan määrätyillä Ruslania Books Oy:n työntekijöillä sekä Ruslania Books Oy:n toimeksiannosta ja lukuun toimivien yritysten työntekijöillä ja asiamiehillä on oikeus käsitellä rekisteriä ja ylläpitää sen tietoja. Kullakin määritellyllä käyttäjällä on oma henkilökohtainen käyttäjätunnus ja salasana. Jokainen käyttäjä on saamistaan tiedoista vaitiolovelvollinen salassapitovelvoitteen mukaisesti ja nojalla.</p>
