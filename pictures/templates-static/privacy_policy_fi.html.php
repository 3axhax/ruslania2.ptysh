<h3>Henkilötietojen käyttö</h3>
<p>Ruslania Books Oy käsittelee henkilötietoja henkilötietolain mukaisesti ja huolehtii yksityisyyden suojan toteutumisesta henkilötietojen käsittelyssä. Henkilötietoja käsitellään Ruslania Books Oy:n palvelujen, toiminnan ja asiakassuhteiden hoitamista varten. Tietoja käytetään myös asiakkaille suunnattuun markkinointiin. Tietoja hankitaan rekisteröidyltä itseltään tai hänen edustajiltaan, viranomaisten pitämistä julkisista rekistereistä, luottotoimintaa harjoittavilta yrityksiltä, luottotieto- ja asiakashäiriörekistereistä sekä maistraatilta.</p>

<p>Liike- tai palvelutapahtuman niin vaatiessa Ruslania Books tallentaa viestejä sekä tiedon asiakkaan tunnistamisesta varmistaakseen viestin sisällön ja henkilön oikeellisuuden.</p>
