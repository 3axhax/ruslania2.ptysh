Was bedeutet es, wenn ein DVD-Film eine Bemerkung „Zone 5“ („Region 5“)  hat?<br><br>
Die DVDs haben ein besonderes Regionsystem.<br>
Die Welt ist auf sechs Regionen aufgeteilt, jede Region hat einen Code.<br><br>
1: USA und Kanada<br>
2: Europa und Japan, South Africa, and Middle East (including Egypt)<br>
3: Süd- und Ostasien (inkl. Hongkong)<br>
4: Australien, Neuseeland, Zentralamerika, Mexiko, Südamerika<br>
5: Osteuropa (die ehemalige Sowjetunion), Indien, Afrika, Nordkorea und Mongolien<br>
6: China
<br><br>
D.h. In Russland gilt der Regionalcode 5 und in Finnland der 2.
<br><br>
Es gibt auch einen s.g. Regionalcode 0, oder „all region“ - d.h. codefrei:
die DVD kann mit einem DVD-Spieler einer beliebigen Region abgespielt werden.<br>
Es ist auch möglich, den Regionalcode umzugehen; dies muss man beim Verkäufer des DVD-Players nachfragen. Ruslania kann bezüglichdessen keine Beratung geben.
<br><br>
Wenn Sie eine DVD mit dem Regionalcode 5 kaufen möchten, und Sie nicht sicher sind, ob Ihr DVD-Spieler den Film abspielen kann, bitte wenden Sie sich an den Verkäufer. 
Bei Ruslania ist es unmöglich, den DVD- oder CD-Verkauf rückgängig zu machen, wenn die Verpackung bereits geöffnet ist.<br><br>
Unabhängig von dem Regionalcode sollten alle DVDs mit dem DVD-fähigen Computer spielbar sein. Wenn Ihr DVD-Spieler die von Ihnen gekaufte DVD nicht abspielen kann, 
probieren Sie die DVD mit Ihrem Computer aus. Ruslania nimmt nur die beim Versand geschädigte Ware zurück, und nicht die wegen Regionalcode unabspielbare DVDs. 
