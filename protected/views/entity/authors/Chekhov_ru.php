    <p class="title__m title__bold">Антон Павлович Чехов</p>
    <p class="title__m">Россия, 29.01.1860 - 15.07.1904</p>
    <img SRC="http://www.ruslania.com/templates-html/books-authors/Image/Chekhov.jpg" width="200" class="author__img" />

    Антон Павлович Чехов родился в Таганроге. Обучался в греческой школе, затем в классической гимназии.
    В 1876 году отец Чехова разорился и был вынужден бежать в Москву, а Антон Павлович остался в Таганроге.<br><br>

    Свои первые литературные работы Чехов под разными псевдонимами посылает в столичные журналы; среди его
    первых произведений - не сохранившиеся комедии "Нашла коса на камень" и "Недаром курица пела", а также
    солидная драма "Безотцовщина".<br><br>

    В 1879 году Чехов поступил на медицинский факультет Московского университета.
    Он много пишет и к 1882 году выпускает первый сборник "Шалость". С конца 1882 года Чехов сотрудничает с
    петербургским журналом "Осколки". Появляются рассказы, известные сейчас любому школьнику: "Двое в одном",
    "Сущая правда", "Смерть чиновника", "Толстый и тонкий".<br><br>

    С 1885 года Чехов, не прекращая сотрудничества с
    редакцией "Осколков", начал печататься в разделе "Летучие заметки" "Петербургской газеты". На страницах
    газеты публикуются рассказы "Лошадиная фамилия", "Тоска", "Ванька", "Беглец". Эти рассказы также выходят
    в свет в сборниках "Пестрые рассказы" и "Невинные речи".<br><br>

    Осенью 1887 года Чехов заканчивает комедию "Иванов",
    через некоторое время поставленную в Московском театре Корша. Пьеса вызвала бурю споров. После "Иванова"
    на свет появляются водевили "Медведь" и "Предложение". В октябре 1896 года Александринский театр ставит
    "Чайку", но публика спектакль не принимает. В 1898 году К.С.Станиславский и Вл.Немирович-Данченко встретились
    с Чеховым и договорились о постановке "Чайки"во МХАТе. Пьеса имела грандиозный успех. Две последующие
    пьесы - "Три сестры" и "Вишневый сад" - Чехов написал специально для МХАТа.<br><br>

    В 1900 году Чехов был избран
    почетным академиком пушкинского отделения РАН. В 1902 году после приказа царя, аннулировавшего избрание
    Горького академиком, Чехов и В.Г.Короленко отказываются от этого звания. В 1901 писатель женился на актрисе
    МХАТа О.Л.Книппер.<br><br>

    15 июля 1904 года, в курортном городе Баденвейлере, Чехов умер от туберкулеза.
    
